static void test_csprng(void) {
    // Sanity check: two successive draws differ; output is non-zero.
    uint8_t a[64], b[64];
    kcsprng_read(a, sizeof(a));
    kcsprng_read(b, sizeof(b));
    int all_zero = 1; for (int i = 0; i < 64; i++) if (a[i]) { all_zero = 0; break; }
    check("kcsprng non-zero output", !all_zero);
    check("kcsprng successive draws differ", !memeq(a, b, 64));
}

void crypto_selftest(void) {
    s_pass = s_fail = 0;
    kprintf("[crypto_test] starting\n");
    test_sha256();
    test_sha384();
    test_hmac_sha256();
    test_hkdf_sha256();
    test_chacha20();
    test_poly1305();
    test_chacha20_poly1305();
    test_aes128();
    test_aes128_gcm();
    test_aes256_gcm();
    test_csprng();
    if (s_fail == 0) {
        kprintf("[crypto_test] SELF-TEST PASSED (%d vectors)\n", s_pass);
    } else {
        kprintf("[crypto_test] SELF-TEST FAILED (%d pass, %d fail)\n",
                s_pass, s_fail);
    }
}
