// ── MSR helpers ───────────────────────────────────────────────────────────
#define MSR_EFER    0xC0000080
#define MSR_STAR    0xC0000081
#define MSR_LSTAR   0xC0000082
#define MSR_SFMASK  0xC0000084

static inline void wrmsr(uint32_t msr, uint64_t val) {
    __asm__ volatile("wrmsr" : : "c"(msr),
                     "a"((uint32_t)(val & 0xFFFFFFFF)),
                     "d"((uint32_t)(val >> 32)));
}

static inline uint64_t rdmsr(uint32_t msr) {
    uint32_t lo, hi;
    __asm__ volatile("rdmsr" : "=a"(lo), "=d"(hi) : "c"(msr));
    return ((uint64_t)hi << 32) | lo;
}

// Forward declarations for the fd-lookup fast path (defined further down).
static vfs_file_t* fdget(uint64_t fd);
static void        fdput(vfs_file_t* f);
static vfs_file_t* fd_to_file(uint64_t fd);

// ── syscall_init ──────────────────────────────────────────────────────────
void syscall_init(void) {
    wrmsr(MSR_EFER, rdmsr(MSR_EFER) | 1);
    wrmsr(MSR_STAR, ((uint64_t)0x0008 << 32) | ((uint64_t)0x0018 << 48));
    wrmsr(MSR_LSTAR, (uint64_t)syscall_entry);
    wrmsr(MSR_SFMASK, (1 << 9) | (1 << 10));
}

// ── sys_write ─────────────────────────────────────────────────────────────
static uint64_t sys_write(uint64_t fd, uint64_t buf, uint64_t len) {
    vfs_file_t* f = fdget(fd);
    if (!f) return (uint64_t)-EBADF;
    if (!f->write) { fdput(f); return (uint64_t)-EBADF; }
    if (f->rights != 0 && !rights_check(f->rights, RIGHT_WRITE)) {
        fdput(f); return (uint64_t)-EACCES;
    }
    int64_t r = vfs_write(f, (const void*)buf, len);
    fdput(f);
    if (r < 0) return (uint64_t)r;
    return (uint64_t)r;
}

// ── user_buf_prefault ─────────────────────────────────────────────────────
// Demand-page every not-yet-mapped page in [addr, addr+len) for the current
// task.  This prevents kernel-mode page faults when vfs_read() writes directly
// into a user buffer that spans pages not yet backed by physical frames.
//
// Must be called while the process's PML4 is active in CR3 (i.e. inside a
// syscall handler, before any address-space switch).
// Returns 0 if the whole range is backed by user VMAs (and now mapped), or
// -1 if any page has NO VMA / can't be backed.  Callers writing into the
// range (copy_to_user etc.) MUST treat -1 as -EFAULT: a bad user pointer
// (e.g. a scribbled cleartid_addr 0x0000680073006900 — canonical but
// unmapped) would otherwise #PF in the subsequent memcpy and panic the
// kernel ("kernel #PF unrecoverable") instead of failing cleanly.
static int user_buf_prefault(virt_addr_t addr, size_t len) {
    if (!len || !g_current) return 0;
    mm_t* mm = g_current->mm_shared ? g_current->mm_shared->mm : NULL;
    if (!mm) return 0;

    virt_addr_t page = addr & ~(virt_addr_t)0xFFF;
    virt_addr_t end  = (addr + len + 0xFFF) & ~(virt_addr_t)0xFFF;
    phys_addr_t pml4 = vmm_current_pml4();  // current CR3 = this process's PML4

    for (; page < end; page += 0x1000) {
        // Walk VMA list under RCU — snapshot the flags we need, then
        // drop the reader before touching page tables.
        uint32_t vma_flags;
        rcu_read_lock();
        {
            vma_t* vma = mm_vma_find(mm, page);
            if (!vma) { rcu_read_unlock(); return -1; }  // no VMA → bad pointer
            vma_flags = vma->flags;
        }
        rcu_read_unlock();

        // Check if this page is already present.  vmm_page_unmap returns 0 and
        // does nothing if the PTE is absent, so we use it safely as a probe only
        // when we need to distinguish. Simpler: just try to map; if the PTE is
        // already present vmm_page_map re-writes it with the same flags (harmless).
        // But we must avoid allocating a frame when the page is already mapped.
        // Use a volatile read of the PTE via the HHDM — walk the 4-level table.
        {
            // PT walk: PML4[39:47] → PDPT[30:38] → PD[21:29] → PT[12:20].
            uint64_t* pml4v = (uint64_t*)(pml4 + HHDM_OFFSET);
            uint64_t  e4 = pml4v[(page >> 39) & 0x1FF];
            if (!(e4 & PAGE_PRESENT)) goto map_page;
            uint64_t* pdpt = (uint64_t*)((e4 & PAGE_ADDR_MASK) + HHDM_OFFSET);
            uint64_t  e3 = pdpt[(page >> 30) & 0x1FF];
            if (!(e3 & PAGE_PRESENT)) goto map_page;
            uint64_t* pd = (uint64_t*)((e3 & PAGE_ADDR_MASK) + HHDM_OFFSET);
            uint64_t  e2 = pd[(page >> 21) & 0x1FF];
            if (!(e2 & PAGE_PRESENT)) goto map_page;
            uint64_t* pt = (uint64_t*)((e2 & PAGE_ADDR_MASK) + HHDM_OFFSET);
            uint64_t  e1 = pt[(page >> 12) & 0x1FF];
            if (e1 & PAGE_PRESENT) continue;  // already mapped — skip
        }

    map_page:;
        // Allocate a frame, zero it, map it with the VMA's permissions.
        phys_addr_t frame = pmm_buddy_alloc(0);
        if (frame == PMM_INVALID_ADDR) return -1;  // OOM — can't back the range

        // Zero the full page (512 × 8 bytes = 4096).
        __builtin_memset((void*)(frame + HHDM_OFFSET), 0, PAGE_SIZE);

        if (!vmm_page_map(pml4, page, frame, mm_vma_pte_flags(vma_flags))) {
            pmm_buddy_free(frame, 0);
            return -1;
        }
    }
    return 0;
}

// ── sys_read ──────────────────────────────────────────────────────────────
static uint64_t sys_read(uint64_t fd, uint64_t buf, uint64_t len, uint64_t flags) {
    (void)flags;
    vfs_file_t* f = fdget(fd);
    if (!f) return (uint64_t)-EBADF;
    if (f->rights != 0 && !rights_check(f->rights, RIGHT_READ)) {
        fdput(f); return (uint64_t)-EACCES;
    }
    if (user_buf_prefault((virt_addr_t)buf, (size_t)len) != 0) {
        fdput(f); return (uint64_t)-EFAULT;
    }
    int64_t r = vfs_read(f, (void*)buf, len);
    fdput(f);
    return (uint64_t)r;
}

// ── sys_open ──────────────────────────────────────────────────────────────
// open(path_ptr, flags, mode) → fd or -errno
// flags: O_RDONLY/O_WRONLY/O_RDWR/O_CREAT/O_EXCL/O_TRUNC/O_APPEND/O_NONBLOCK/O_CLOEXEC
// mode: used for umask masking on O_CREAT.
// Non-static — exposed for io_uring opcode dispatch.
uint64_t sys_open(uint64_t path_ptr, uint64_t flags, uint64_t mode) {
    if (!g_current || !path_ptr) return (uint64_t)-EINVAL;

    // Copy null-terminated path from user space (max 511 to leave room for cwd prefix).
    char raw[512];
    const char* upath = (const char*)path_ptr;
    uint64_t i = 0;
    while (i < 511 && upath[i]) { raw[i] = upath[i]; i++; }
    raw[i] = '\0';
    if (i == 0) return (uint64_t)-EINVAL;

    /* Log the path for traced comms — the generic syscall_dispatch
     * tracer only sees the user pointer, which isn't useful when
     * diagnosing "what file did foot try to open?" */
    if (g_current && comm_match(g_current->comm)) {
        pr_debug("sys", "  open(\"%s\", flags=0x%lx)",
                  raw, (uint64_t)flags);
    }

    // Resolve relative paths: prepend cwd if path does not start with '/'.
    char path[512];
    if (raw[0] != '/') {
        const char* cwd = g_current->cwd;
        uint64_t clen = 0;
        while (cwd[clen]) clen++;
        uint64_t j = 0;
        for (; j < clen && j < 510; j++) path[j] = cwd[j];
        // Ensure cwd ends with '/'.
        if (j > 0 && path[j-1] != '/') path[j++] = '/';
        for (uint64_t k = 0; raw[k] && j < 511; k++, j++) path[j] = raw[k];
        path[j] = '\0';
    } else {
        for (uint64_t k = 0; k <= i; k++) path[k] = raw[k];
    }

    vfs_file_t* f = NULL;

    // ── Permission check + existence test via unified fs_lookup ──────────
    // Covers both virtfs (/proc, /dev) and ext2 in one call.
    // fsn.inode_nr is the already-resolved ext2 inode — no second lookup needed.
    uint8_t open_need = ACL_PERM_READ;
    {
        int oflags_acc = (int)(flags & 3);
        if (oflags_acc == O_WRONLY || oflags_acc == O_RDWR) open_need |= ACL_PERM_WRITE;
    }
    fs_node_t fsn;
    int fsr = fs_lookup(path, &g_current->cred, open_need, &fsn);
    if (fsr == 0 && (flags & O_CREAT) && (flags & O_EXCL))
        return (uint64_t)-EEXIST;
    if (fsr != 0 && fsr != -ENOENT) {
        if (path[0]=='/' && path[1]=='t' && path[2]=='m' && path[3]=='p')
            kprintf("[open-dbg] %s flags=0x%x -> %d\n", path, (uint32_t)flags, fsr);
        return (uint64_t)(int64_t)fsr;
    }
    if (fsr == -ENOENT && !(flags & O_CREAT)) {
        if (path[0]=='/' && path[1]=='t' && path[2]=='m' && path[3]=='p')
            kprintf("[open-dbg] %s ENOENT flags=0x%x\n", path, (uint32_t)flags);
        return (uint64_t)-ENOENT;
    }

    // Virtual path — dispatch to backend; permission already checked by fs_lookup.
    if (fsr == 0 && fsn.is_virtual) {
        if (path[0]=='/' && path[1]=='p') {
            // /proc
            f = proc_open(path);
            if (!f) return (uint64_t)-ENOENT;
            goto got_file;
        }
        // /dev — dispatch by device name
        const char* dev = path + 5; // skip "/dev/"
        uint8_t match_kbd    = (dev[0]=='k' && dev[1]=='b' && dev[2]=='d' && dev[3]=='\0');
        uint8_t match_kbdraw = (dev[0]=='k' && dev[1]=='b' && dev[2]=='d' && dev[3]=='r'
                             && dev[4]=='a' && dev[5]=='w' && dev[6]=='\0');
        uint8_t match_vga    = (dev[0]=='v' && dev[1]=='g' && dev[2]=='a' && dev[3]=='\0');
        uint8_t match_mouse  = (dev[0]=='m' && dev[1]=='o' && dev[2]=='u' && dev[3]=='s'
                             && dev[4]=='e' && dev[5]=='\0');
        uint8_t match_dsp    = (dev[0]=='d' && dev[1]=='s' && dev[2]=='p' && dev[3]=='\0');
        uint8_t match_tty    = (dev[0]=='t' && dev[1]=='t' && dev[2]=='y' && dev[3]=='\0');
        uint8_t match_tty0   = (dev[0]=='t' && dev[1]=='t' && dev[2]=='y' && dev[3]=='0' && dev[4]=='\0');
        uint8_t match_null   = (dev[0]=='n' && dev[1]=='u' && dev[2]=='l' && dev[3]=='l' && dev[4]=='\0');
        uint8_t match_zero   = (dev[0]=='z' && dev[1]=='e' && dev[2]=='r' && dev[3]=='o' && dev[4]=='\0');
        uint8_t match_urnd   = (dev[0]=='u' && dev[1]=='r' && dev[2]=='a' && dev[3]=='n'
                             && dev[4]=='d' && dev[5]=='o' && dev[6]=='m' && dev[7]=='\0');
        // /dev/input/event<N>: parse the decimal number so every
        // registered input_device_t gets its own node, not just event0.
        int match_eventn = -1;
        if (dev[0]=='i' && dev[1]=='n' && dev[2]=='p' && dev[3]=='u'
            && dev[4]=='t' && dev[5]=='/' && dev[6]=='e' && dev[7]=='v'
            && dev[8]=='e' && dev[9]=='n' && dev[10]=='t' && dev[11] >= '0' && dev[11] <= '9') {
            int n = 0; int i = 11;
            while (dev[i] >= '0' && dev[i] <= '9') { n = n*10 + (dev[i]-'0'); i++; }
            if (dev[i] == '\0') match_eventn = n;
        }
        uint8_t match_dri0   = (dev[0]=='d' && dev[1]=='r' && dev[2]=='i' && dev[3]=='/'
                             && dev[4]=='c' && dev[5]=='a' && dev[6]=='r' && dev[7]=='d'
                             && dev[8]=='0' && dev[9]=='\0');
        uint8_t match_ptmx   = (dev[0]=='p' && dev[1]=='t' && dev[2]=='m'
                             && dev[3]=='x' && dev[4]=='\0');
        // /dev/pts/<N>: claim the slave side of an existing pty.
        int match_ptsn = -1;
        if (dev[0]=='p' && dev[1]=='t' && dev[2]=='s' && dev[3]=='/'
            && dev[4] >= '0' && dev[4] <= '9') {
            int n = 0; int i = 4;
            while (dev[i] >= '0' && dev[i] <= '9') { n = n*10 + (dev[i]-'0'); i++; }
            if (dev[i] == '\0') match_ptsn = n;
        }
        if      (match_tty)    f = tty_open(0);
        else if (match_tty0)   f = tty_open(0);
        else if (match_kbdraw) f = vfs_kbdraw_open();
        else if (match_eventn >= 0) { extern vfs_file_t* evdev_open_device(uint32_t); f = evdev_open_device((uint32_t)match_eventn); }
        else if (match_dri0)   { extern vfs_file_t* vfs_drm_open(void); f = vfs_drm_open(); }
        else if (match_ptmx)   { extern vfs_file_t* pty_open_master(void); f = pty_open_master(); }
        else if (match_ptsn >= 0) { extern vfs_file_t* pty_open_slave_by_index(int); f = pty_open_slave_by_index(match_ptsn); }
        else if (match_kbd)    f = vfs_kbd_open();
        else if (match_vga)    f = vfs_vga_open();
        else if (match_mouse)  f = vfs_mouse_open();
        else if (match_dsp)    f = vfs_dsp_open();
        else if (match_null)   f = vfs_null_open();
        else if (match_zero)   f = vfs_zero_open();
        else if (match_urnd)   f = vfs_urandom_open();
        else return (uint64_t)-ENOENT;
        // fall through to got_file
    }

    if (!f) {
        // ext2 path.  fsn.inode_nr is already resolved by fs_lookup above —
        // no second ext2_lookup_path call needed.
        if (fsr == 0) {
            // File exists and fsn.inode_nr is already resolved by fs_lookup.
            // Open by inode directly — no second path walk.
            f = ext2_open_ino(fsn.inode_nr, path);
            if (f) goto got_file;
        } else {
            // fsr == -ENOENT && O_CREAT: check write+exec on parent directory.
            char parent[512];
            uint64_t plen = 0;
            while (path[plen]) plen++;
            uint64_t slash = plen;
            while (slash > 0 && path[slash] != '/') slash--;
            if (slash == 0) { parent[0] = '/'; parent[1] = '\0'; }
            else { __builtin_memcpy(parent, path, slash); parent[slash] = '\0'; }
            // Use fs_lookup for the parent too — avoids a raw ext2_lookup_path.
            fs_node_t par_fsn;
            int par_r = fs_lookup(parent, &g_current->cred,
                                   ACL_PERM_WRITE | ACL_PERM_EXEC, &par_fsn);
            if (par_r != 0) return (uint64_t)(int64_t)par_r;
        }

        f = ext2_open(path);
        if (!f && (flags & O_CREAT)) {
            mode &= ~(uint64_t)g_current->umask;
            (void)mode;
            if (!ext2_create(path)) return (uint64_t)-EIO;
            f = ext2_open(path);
        }
    }

    if (!f) return (uint64_t)-ENOENT;

got_file:
    // ── unveil check ─────────────────────────────────────────────────────
    // Paths not in the unveil table return ENOENT (as if they don't exist).
    // The virtual mounts (/dev, /proc) are exempt (always accessible via this
    // path).  Use virtfs_is_virtual -- the single boundary-correct "is virtual"
    // predicate -- NOT a hand-rolled prefix test: the old `path[1..3]=='dev'`
    // form matched any real on-disk file named /devsecrets, /processData, etc.,
    // letting it skip the unveil sandbox entirely (sandbox escape).
    if (g_current->unveil.count > 0 && !virtfs_is_virtual(path)) {
        uint8_t need_u = UNVEIL_READ;
        int oflags_acc = (int)(flags & 3);
        if (oflags_acc == O_WRONLY || oflags_acc == O_RDWR) need_u |= UNVEIL_WRITE;
        if (flags & O_CREAT)                                 need_u |= UNVEIL_CREATE;
        if (!unveil_check(&g_current->unveil, path, need_u)) {
            vfs_close(f);
            return (uint64_t)-ENOENT;
        }
    }

    // ── Stamp fd rights from open flags ───────────────────────────────────
    f->rights = rights_from_oflags((int)(flags & 3), 0 /* exec_ok: not checked here */);

    // Enforce access mode: strip write for O_RDONLY (belt-and-suspenders).
    if ((flags & 3) == O_RDONLY) f->write = NULL;

    // Propagate O_APPEND/O_NONBLOCK into vfs_file_t.flags.
    if (flags & O_APPEND)    f->flags |= O_APPEND;
    if (flags & O_NONBLOCK)  f->flags |= O_NONBLOCK;

    // O_TRUNC: truncate to zero on open (write modes only).
    if ((flags & O_TRUNC) && (flags & (O_WRONLY | O_RDWR)))
        ext2_truncate(path);

    // Find the lowest free fd.  The writer lock serialises grow and
    // slot allocation against concurrent installs / closes / dups.
    task_files_t* tf = g_current->files_shared;
    spin_lock(&tf->lock);
    for (uint32_t fd = 0; ; fd++) {
        if (fd >= tf->ft->cap) {
            if (!fd_table_grow(tf)) {
                spin_unlock(&tf->lock);
                vfs_close(f);
                return (uint64_t)-ENFILE;
            }
        }
        if (!tf->ft->fd_table[fd]) {
            tf->ft->fd_table[fd] = f;
            tf->ft->fd_flags[fd] = (flags & O_CLOEXEC) ? FD_CLOEXEC : 0;
            spin_unlock(&tf->lock);
            return (uint64_t)fd;
        }
    }
}

// ── sys_close ─────────────────────────────────────────────────────────────
// close(fd) → 0 or -1
// Non-static so other subsystems (io_uring, etc.) can invoke the
// canonical close semantics without duplicating the fd-table walk.
uint64_t sys_close(uint64_t fd) {
    if (!g_current || !g_current->files_shared) return (uint64_t)-EBADF;
    task_files_t* tf = g_current->files_shared;
    spin_lock(&tf->lock);
    if (fd >= tf->ft->cap) { spin_unlock(&tf->lock); return (uint64_t)-EBADF; }
    vfs_file_t* f = tf->ft->fd_table[fd];
    if (!f) { spin_unlock(&tf->lock); return (uint64_t)-EBADF; }
    tf->ft->fd_table[fd] = NULL;
    tf->ft->fd_flags[fd] = 0;
    spin_unlock(&tf->lock);
    // Drop ref outside the lock: vfs_close may sleep or take driver locks.
    vfs_close(f);
    return 0;
}

// ── sys_brk ───────────────────────────────────────────────────────────────
// brk(new_brk): set the top of the heap to new_brk.
//   - new_brk == 0: return current brk (Linux convention for querying).
//   - new_brk < brk_start: reject.
//   - new_brk > brk: grow heap — add/extend VMA, pages are demand-paged.
//   - new_brk < brk: shrink heap — remove VMA pages above new_brk
//     and unmap+free them.
// Returns new brk on success, (uint64_t)-1 on error.
static uint64_t sys_brk(uint64_t new_brk_raw) {
    if (!g_current || !g_current->mm_shared->mm) return (uint64_t)-EINVAL;

    mm_t* mm = g_current->mm_shared->mm;

    // Query current brk.
    if (new_brk_raw == 0) return mm->brk;

    virt_addr_t new_brk = new_brk_raw; // exact value; demand-paging handles granularity

    // Reject below heap start.
    if (new_brk < mm->brk_start) return (uint64_t)-EINVAL;

    // Reject if new_brk would overlap with the stack region.
    // (Stack starts at VMM_USER_STACK_TOP - VMM_USER_STACK_PAGES * PAGE_SIZE)
    virt_addr_t stack_floor = VMM_USER_STACK_TOP - VMM_USER_STACK_PAGES * PAGE_SIZE;
    if (new_brk >= stack_floor) return (uint64_t)-ENOMEM;

    virt_addr_t old_brk = mm->brk;

    if (new_brk == old_brk) return old_brk;

    if (new_brk > old_brk) {
        // ── Grow heap ─────────────────────────────────────────────────────
        // Rather than remove+add (which races readers and loses flags),
        // ask mm_vma_remove to drop the old heap VMA (deferred free via
        // call_rcu) and then mm_vma_add to install the enlarged one.
        virt_addr_t heap_vma_start = mm->brk_start;
        virt_addr_t vma_end = (new_brk + PAGE_MASK) & ~PAGE_MASK;

        // mm_vma_remove takes mm->vma_lock internally.
        extern uint8_t mm_vma_remove(mm_t*, virt_addr_t, virt_addr_t);
        // We need to know the old end to pass it in.  Read it under RCU.
        virt_addr_t old_end = 0;
        rcu_read_lock();
        for (vma_t* v = rcu_dereference(mm->vmas); v; v = rcu_dereference(v->next)) {
            if (v->start == heap_vma_start) { old_end = v->end; break; }
        }
        rcu_read_unlock();
        if (old_end)
            mm_vma_remove(mm, heap_vma_start, old_end);

        if (!mm_vma_add(mm, heap_vma_start, vma_end, VMA_R | VMA_W | VMA_ANON))
            return (uint64_t)-ENOMEM;

        mm->brk = new_brk;
        return new_brk;
    }

    // ── Shrink heap ───────────────────────────────────────────────────────
    // Unmap and free physical frames in [new_brk, old_brk).
    // Use pmm_ref_dec (CoW-aware): frame only freed when refcount→0.
    phys_addr_t pml4 = vmm_current_pml4();
    for (virt_addr_t page = new_brk; page < old_brk; page += PAGE_SIZE) {
        phys_addr_t frame;
        if (vmm_page_unmap(pml4, page, &frame))
            pmm_ref_dec(frame);
    }

    // Shrink or remove the heap VMA.  Under mm->vma_lock so mutation
    // is serialised with concurrent mmap/munmap on the same address space.
    virt_addr_t heap_vma_start = mm->brk_start;
    spin_lock(&mm->vma_lock);
    vma_t** pp = &mm->vmas;
    while (*pp) {
        vma_t* v = *pp;
        if (v->start == heap_vma_start) {
            if (new_brk <= heap_vma_start) {
                rcu_assign_pointer(*pp, v->next);
                // Expedited: user-visible brk() return latency matters.
                call_rcu_head(&v->rcu_head, vma_free_rcu, v);
            } else {
                v->end = new_brk;  // in-place shrink — safe under RCU
            }
            break;
        }
        pp = &v->next;
    }
    spin_unlock(&mm->vma_lock);

    mm->brk = new_brk;

    // Remote CPUs running other threads of this process may still hold
    // TLB entries for pages we just freed.  Flush them before returning
    // so a stale translation doesn't allow a racing thread to read/write
    // memory that's already been handed back to the buddy allocator.
    tlb_flush_range(g_current->mm_shared, new_brk, old_brk);
    return new_brk;
}

// ── sys_exit ──────────────────────────────────────────────────────────────
// Reparent direct children to g_init_task, drop the fd table, then zombify.
//
// Releasing files_shared here (matching Linux exit_files()) is load-bearing:
// peers on the other end of a unix socket must see POLLHUP the moment the
// task calls exit(), not whenever the parent eventually calls wait(). Without
// this, a process that exits while its parent is busy (e.g. makaterm exiting
// while its parent loops on something else) leaves its sockets half-open and
// the peer never learns the client is gone. The zombie only needs to retain
// exit_code / pid for waitpid — fds are not part of that contract.
// CLEARTID death notification — runs while the mm is still alive and
// the task can no longer return to userspace.  The joiner may free the
// thread's stack and TLS the instant this fires; nothing below this
// point touches user memory of the dying task.  Also called from the
// signal-terminate path (kernel/proc/signal.c) for killed threads.
void task_notify_cleartid(void) {
    extern int copy_to_user(void* dst_u, const void* src, uint64_t len);
    if (!g_current || !g_current->cleartid_addr) return;
    uint64_t addr = g_current->cleartid_addr;
    g_current->cleartid_addr = 0;
    uint32_t one = 1;
    if (copy_to_user((void*)addr, &one, sizeof(one)) == 0) {
        extern int64_t futex_wake(uint32_t*, uint32_t);
        futex_wake((uint32_t*)addr, 0x7fffffff);
    }
}

static uint64_t sys_exit(uint64_t code) {
    if (g_current) {
        task_notify_cleartid();
        // Reparent every direct child onto g_init_task in one batched
        // CAS splice (task_children_reparent drains our own list — no
        // race, only self touches it here — then CAS-prepends the
        // whole chain onto init's list).  If we ARE init or init is
        // absent, just clear our list; orphaned children live until
        // their own exit reaps them.
        if (g_init_task && g_init_task != g_current) {
            task_children_reparent(g_current, g_init_task);
        } else {
            g_current->children = NULL;
        }

        // Drop the fd table now so peers (pipes, sockets, ttys) see EOF
        // immediately rather than at reap time.
        if (g_current->files_shared) {
            task_files_release(g_current->files_shared);
            g_current->files_shared = NULL;
        }

        // Log page-cache stats for demand-paged (VMA_FILE) processes.
        if (g_current->pf_disk || g_current->pf_cache) {
            uint32_t total = g_current->pf_disk + g_current->pf_cache;
            uint32_t pct   = total ? (g_current->pf_cache * 100u / total) : 0u;
            kprintf("[pf] %s: %u disk  %u cache  (%u%% warm)\n",
                    g_current->comm,
                    g_current->pf_disk, g_current->pf_cache, pct);
        }

        g_current->exit_code = (int32_t)(int)code;

        // A pthread (TASK_FLAG_THREAD) is NOT a child anyone wait()s on —
        // userland joins via the futex 'done' word (SYS_SET_CLEARTID,
        // already signalled above), not sys_wait.  So a thread must NOT
        // become a wait()-able zombie (it would linger forever — a kernel
        // task-struct + pid leak per thread) and must NOT send SIGCHLD to
        // the PROCESS's parent (a font thread exiting would spuriously
        // wake dwl's child reaper).  Self-reap instead: TASK_DEAD is
        // collected by the idle reaper right after we switch away, which
        // refcount-releases the shared mm/files (task_free_rcu).
        if (g_current->flags & TASK_FLAG_THREAD) {
            g_current->state = TASK_DEAD;     // idle reaper frees it
        } else {
            g_current->state = TASK_ZOMBIE;
            sched_add_zombie(g_current);

            // Wake parent and deliver SIGCHLD so it can reap background
            // jobs.  signal_send_pid looks up + delivers under rcu_read_lock
            // so the parent cannot be freed in the window, and skips zombie
            // parents (semantically dead).  signal_send unconditionally calls
            // sched_wake under the target rq_lock, so no racy state check.
            signal_send_pid(g_current->ppid, SIGCHLD);
        }
    }
    sched_yield();
    for (;;) __asm__ volatile("hlt");
    return 0;
}

// ── sys_kill helpers (file-scope so clang accepts them) ───────────────────
static int s_bcast_sig = 0;
static void kill_bcast_cb(task_t* t, void* data) {
    (void)data;
    if (!(t->flags & TASK_FLAG_KTHREAD) && t->state != TASK_ZOMBIE)
        signal_send(t, s_bcast_sig);
}

// ── sys_kill ──────────────────────────────────────────────────────────────
// kill(pid, sig): send signal `sig` to the task with the given pid.
// pid > 0: send to the task with that pid.
// pid == 0: send to all tasks in the caller's thread group.
// pid == -1: broadcast to all tasks (except idle/kernel threads).
static uint64_t sys_kill(uint64_t pid_raw, uint64_t sig_raw) {
    int sig = (int)(int64_t)sig_raw;
    if (sig < 1 || sig >= NSIG) return (uint64_t)-EINVAL;

    int64_t pid = (int64_t)pid_raw;

    if (pid > 0) {
        // O(1) lookup via the pid hash table.  Zombies are kept in
        // pid_ht until fully reaped, but kill() on a zombie is an
        // error (ESRCH) — the task is semantically dead.  Sending to
        // another task goes through signal_send_pid, which does the
        // lookup + delivery under rcu_read_lock so the target cannot be
        // freed in the window (sched_find_pid alone returns a bare
        // pointer with no such guarantee).
        if ((int64_t)g_current->pid == pid) {
            signal_send(g_current, sig);    // self: cannot be freed
            return 0;
        }
        return (uint64_t)signal_send_pid((uint32_t)pid, sig);  // 0 or -ESRCH
    }

    if (pid == 0) {
        // POSIX: signal the CALLER'S PROCESS GROUP (not thread group).
        if (!g_current) return (uint64_t)-ESRCH;
        signal_send_pgrp(g_current->pgid, sig);
        return 0;
    }

    if (pid < -1) {
        // kill(-pgid): every member of process group |pid|.  This is
        // how a terminal emulator HUPs its shell's job on shutdown
        // (foot does exactly this).  This case used to fall through
        // to the -1 broadcast below, SIGHUP'ing every process on the
        // system — login, svcmgr and net included.
        signal_send_pgrp((uint32_t)(-pid), sig);
        return 0;
    }

    // pid == -1: broadcast to all non-kernel tasks.
    if (g_current && !(g_current->flags & TASK_FLAG_KTHREAD))
        signal_send(g_current, sig);
    s_bcast_sig = sig;
    sched_for_each(kill_bcast_cb, NULL);
    return 0;
}

// ── sys_fork ──────────────────────────────────────────────────────────────
static uint64_t sys_fork(void) {
    if (!g_current || !g_current->mm_shared->mm) return (uint64_t)-EINVAL;
    // Saved user context comes from the per-task kernel-stack frame —
    // the per-CPU scratch goes stale if this task was preempted and
    // migrated between syscall entry and this point.
    syscall_kframe_t* kf = SYSCALL_KFRAME(g_current);
    task_t* child = task_fork(g_current,
                              kf->rip,
                              kf->rflags,
                              kf->rsp,
                              kf->rbp,
                              kf->rbx,
                              kf->r12,
                              kf->r13,
                              kf->r14,
                              kf->r15);
    if (!child) return (uint64_t)-ENOMEM;
    task_child_add(g_current, child);
    sched_add(child);
    return (uint64_t)child->pid;
}

// ── sys_exec (execve) ─────────────────────────────────────────────────────
// execve(path_ptr, argv_ptr, envp_ptr)
//
// path_ptr — user pointer to NUL-terminated path string
// argv_ptr — user pointer to NULL-terminated array of user char* pointers
// envp_ptr — user pointer to NULL-terminated array of user char* pointers
//
// Replaces the current address space with the ELF at path.
// Closes all fds with FD_CLOEXEC set.
// Inherits pgid, sid, cwd, umask.
// On success does not return (triggers sysretq to new entry point via
// g_exec_requested mechanism).
static uint64_t sys_exec(uint64_t path_ptr, uint64_t argv_ptr, uint64_t envp_ptr) {
    if (!g_current || !g_current->mm_shared->mm) return (uint64_t)-EINVAL;
    if (!path_ptr) return (uint64_t)-EINVAL;

    // ── Copy path from user space ─────────────────────────────────────────
    char path[512];
    const char* upath = (const char*)path_ptr;
    uint64_t plen = 0;
    while (plen < 511 && upath[plen]) { path[plen] = upath[plen]; plen++; }
    path[plen] = '\0';
    if (plen == 0) return (uint64_t)-EINVAL;

    // Resolve relative path using cwd.
    char resolved[512];
    if (path[0] != '/') {
        const char* cwd = g_current->cwd;
        uint64_t clen = 0; while (cwd[clen]) clen++;
        uint64_t j = 0;
        for (; j < clen && j < 510; j++) resolved[j] = cwd[j];
        if (j > 0 && resolved[j-1] != '/') resolved[j++] = '/';
        for (uint64_t k = 0; path[k] && j < 511; k++, j++) resolved[j] = path[k];
        resolved[j] = '\0';
    } else {
        for (uint64_t k = 0; k <= plen; k++) resolved[k] = path[k];
    }

    // ── Copy argv from user space (up to 256 args, 4KiB each) ────────────
    #define MAX_ARGS  256
    #define MAX_ARG_LEN 4096
    char* k_argv[MAX_ARGS + 1];
    uint32_t argc = 0;
    if (argv_ptr) {
        const uint64_t* uargv = (const uint64_t*)argv_ptr;
        while (argc < MAX_ARGS) {
            uint64_t ustr = uargv[argc];
            if (!ustr) break;
            const char* s = (const char*)ustr;
            uint64_t l = 0; while (l < MAX_ARG_LEN && s[l]) l++;
            char* ks = kmalloc(l + 1);
            if (!ks) goto oom;
            __builtin_memcpy(ks, s, l);
            ks[l] = '\0';
            k_argv[argc++] = ks;
        }
    }
    k_argv[argc] = NULL;

    // ── Copy envp from user space (up to 512 vars) ────────────────────────
    #define MAX_ENVS 512
    char* k_envp[MAX_ENVS + 1];
    uint32_t envc = 0;
    if (envp_ptr) {
        const uint64_t* uenvp = (const uint64_t*)envp_ptr;
        while (envc < MAX_ENVS) {
            uint64_t ustr = uenvp[envc];
            if (!ustr) break;
            const char* s = (const char*)ustr;
            uint64_t l = 0; while (l < MAX_ARG_LEN && s[l]) l++;
            char* ks = kmalloc(l + 1);
            if (!ks) goto oom;
            __builtin_memcpy(ks, s, l);
            ks[l] = '\0';
            k_envp[envc++] = ks;
        }
    }
    k_envp[envc] = NULL;

    // ── Execute permission check (path-aware walk + exec bit) ────────────
    int exec_err = 0;
    uint32_t exec_ino = ext2_lookup_path(resolved, &g_current->cred, &exec_err);
    if (!exec_ino) { if (exec_err) return (uint64_t)(int64_t)exec_err; goto enoent; }
    {
        ext2_inode_t exec_inode;
        if (!ext2_read_inode(exec_ino, &exec_inode)) goto enoent;
        inode_perm_t exec_ip = {
            .uid = exec_inode.i_uid, .gid = exec_inode.i_gid,
            .mode = exec_inode.i_mode & 0x1FF,
            .inode_nr = exec_ino, .dev = 0, .nosuid = 0,
        };
        uint32_t setuid_uid = 0xFFFFFFFFu;
        if (vfs_check_exec(&exec_ip, &g_current->cred, &setuid_uid) != 0)
            goto enoent;
    }

    // ── Load ELF — lazy (file-backed VMAs, pages faulted in on demand) ───────
    // exec_ino already resolved above; ext2_open_ino skips the second walk.
    // Read only the ELF header + program headers (first PAGE_SIZE bytes).
    // elf_load_into parses the phdrs from this buffer and creates VMA_FILE
    // VMAs backed by `f`; no pages are allocated here.  The file ref is
    // dup'd per VMA; we vfs_close our own ref below.
    vfs_file_t* f = ext2_open_ino(exec_ino, resolved);
    if (!f) { goto enoent; }

    // kmalloc so the buffer is HHDM-mapped — ahci_submit_hhdm requires
    // buf in HHDM (phys = va - HHDM_OFFSET).  Kernel stack is at
    // 0xFFFFFFFF80... which is NOT HHDM, causing DMA to a wrong PA.
    uint8_t* hdr_buf = (uint8_t*)kmalloc(PAGE_SIZE);
    if (!hdr_buf) { vfs_close(f); goto oom; }
    uint64_t t0 = tsc_read_ns();
    int64_t hdr_n = f->read(f, hdr_buf, PAGE_SIZE);
    uint64_t t1 = tsc_read_ns();
    if (hdr_n < (int64_t)sizeof(Elf64_Ehdr)) { kfree(hdr_buf); vfs_close(f); goto enoexec; }
    kprintf("[elf] lazy %s: header %u us (demand-paged)\n",
            resolved, (uint32_t)((t1 - t0) / 1000u));

    phys_addr_t new_pml4;
    mm_t*       new_mm;
    uint64_t    entry;
    uint64_t    phdr_vaddr = 0;
    uint16_t    phnum = 0, phent = 0;

    if (!elf_load_into(hdr_buf, (uint64_t)hdr_n, &new_pml4, &new_mm, &entry,
                       &phdr_vaddr, &phnum, &phent, f)) {
        kfree(hdr_buf);
        vfs_close(f);
        goto enoexec;
    }
    kfree(hdr_buf);
    // VMAs hold their own refs; release ours.
    vfs_close(f);

    // ── Build user stack with argv/envp/auxv ──────────────────────────────
    uint64_t user_rsp = elf_setup_stack(new_pml4,
                                         (const char* const*)k_argv,
                                         (const char* const*)k_envp,
                                         entry, phdr_vaddr, phnum, phent);
    if (!user_rsp) {
        vmm_free_user(new_pml4); pmm_buddy_free(new_pml4, 0); mm_destroy(new_mm);
        goto oom;
    }

    // ── Free kernel copies of argv/envp ───────────────────────────────────
    for (uint32_t i = 0; i < argc; i++) kfree(k_argv[i]);
    for (uint32_t i = 0; i < envc; i++) kfree(k_envp[i]);

    // ── Close FD_CLOEXEC fds ──────────────────────────────────────────────
    {
        task_files_t* files = g_current->files_shared;
        spin_lock(&files->lock);
        fdtable_t* ft = files->ft;
        for (uint32_t i = 0; i < ft->cap; i++) {
            if (ft->fd_table[i] && (ft->fd_flags[i] & FD_CLOEXEC)) {
                vfs_file_t* vf = ft->fd_table[i];
                ft->fd_table[i] = NULL;
                ft->fd_flags[i] = 0;
                spin_unlock(&files->lock);
                vfs_close(vf);
                spin_lock(&files->lock);
                ft = files->ft;  // re-load in case grow raced — unlikely but cheap
            }
        }
        spin_unlock(&files->lock);
    }

    // ── Swap in new address space ─────────────────────────────────────────
    // CRITICAL ORDERING: switch CR3 to the new PML4 *before* freeing the old
    // page-table hierarchy.  The deferred sysret switch (syscall_entry.asm)
    // doesn't load CR3 until the very end of the return path, so freeing the
    // old tables here while CR3 still points at them is a write-after-free:
    // the hardware page-table walker sets A/D bits — and a racing fault could
    // deposit a PTE — into frames already back on the buddy free list.
    // (Observed: 0x..027 = phys|P|W|U|A scribbled into a freed PT page,
    // caught by the PMM free-frame tripwire; whole-DE instability under the
    // exec churn of spawning foot/tofi.)  new_pml4 shares the kernel
    // upper-half mappings (vmm_alloc_pml4 copied PML4[256..511]), so the
    // kernel keeps running across the switch and the remaining exec code
    // touches only kernel state — never the old lower-half image.
    phys_addr_t old_pml4 = g_current->mm_shared->pml4_phys;
    mm_t*       old_mm   = g_current->mm_shared->mm;

    g_current->mm_shared->pml4_phys = new_pml4;
    g_current->mm_shared->mm        = new_mm;
    vmm_switch(new_pml4);   // CR3 = new_pml4; old hierarchy now unreferenced

    vmm_free_user_ex(old_pml4, old_mm);   // safe: no CPU walks old_pml4 now
    pmm_buddy_free(old_pml4, 0);
    mm_destroy(old_mm);

    // Reset signal handlers to SIG_DFL (POSIX: exec clears custom handlers).
    for (int si = 0; si < NSIG; si++) {
        if (g_current->sigstate.handlers[si].sa_handler > SIG_IGN)
            g_current->sigstate.handlers[si].sa_handler = SIG_DFL;
        g_current->sigstate.handlers[si].sa_mask  = 0;
        g_current->sigstate.handlers[si].sa_flags = 0;
    }
    // Unblock all signals (POSIX: exec resets the signal mask).
    g_current->sigstate.blocked = 0;

    // Drop the pre-exec TLS pointer — those addresses belong to the
    // torn-down image.  The new image's crt0 installs fresh TLS via
    // SYS_SET_FS before any %fs access.
    g_current->fs_base = 0;
    __asm__ volatile("wrmsr" :: "c"(0xC0000100u /*MSR_FS_BASE*/),
                     "a"(0), "d"(0));

    // Update comm to the basename of argv[0] (or the resolved path if
    // argv[0] is NULL / empty).  POSIX exec() semantics — every debug
    // print, every /proc/<pid>/status, every ps listing should reflect
    // the new image, not the pre-exec one.
    {
        const char* name = (argc > 0 && k_argv[0]) ? k_argv[0] : resolved;
        // Oops — k_argv[] was already kfree'd above.  Use resolved.
        name = resolved;
        const char* base = name;
        for (const char* p = name; *p; p++) if (*p == '/') base = p + 1;
        uint32_t ci = 0;
        while (ci < 15 && base[ci]) {
            g_current->comm[ci] = base[ci];
            ci++;
        }
        g_current->comm[ci] = '\0';
    }

    // Reset page-fault stats for the new image.
    g_current->pf_disk  = 0;
    g_current->pf_cache = 0;

    // ── Trigger sysretq to new entry point ────────────────────────────────
    g_exec_entry     = entry;
    g_exec_rsp       = user_rsp;
    g_exec_pml4      = new_pml4;
    g_exec_requested = 1;
    return 0;

oom:
    for (uint32_t i = 0; i < argc; i++) kfree(k_argv[i]);
    for (uint32_t i = 0; i < envc; i++) kfree(k_envp[i]);
    return (uint64_t)-ENOMEM;
enoent:
    for (uint32_t i = 0; i < argc; i++) kfree(k_argv[i]);
    for (uint32_t i = 0; i < envc; i++) kfree(k_envp[i]);
    return (uint64_t)-ENOENT;
enoexec:
    for (uint32_t i = 0; i < argc; i++) kfree(k_argv[i]);
    for (uint32_t i = 0; i < envc; i++) kfree(k_envp[i]);
    return (uint64_t)-ENOEXEC;
}

// ── sys_spawn ─────────────────────────────────────────────────────────────
// spawn(path_ptr, argv_ptr, envp_ptr, stdio_ptr, attr_ptr) → child pid, -errno.
//
// Loads an ELF from the given absolute ext2 path into a brand-new address
// space and schedules it.  Parent returns immediately with the child's pid.
//
// path_ptr  — user pointer to NUL-terminated absolute path string
// argv_ptr  — user pointer to NULL-terminated array of user char* (or 0 → {path, NULL})
// envp_ptr  — user pointer to NULL-terminated array of user char* (or 0 → default env)
// stdio_ptr — user pointer to int[3]: stdio fd specs for child's 0/1/2
//               -1  = inherit that fd from parent
//               -2  = /dev/null
//               >=0 = dup that specific parent fd
//               0 (null ptr) = open /dev/tty0 for all three
// attr_ptr  — user pointer to spawn_attr_t, or 0 for no extra attributes
static uint64_t sys_spawn(uint64_t path_ptr, uint64_t argv_ptr,
                           uint64_t envp_ptr, uint64_t stdio_ptr,
                           uint64_t attr_ptr) {
    serial_puts_dbg("[spawn] path="); serial_puts_dbg((const char*)path_ptr); serial_putc_dbg('\n');
    if (!g_current) return (uint64_t)-EINVAL;
    if (!path_ptr)  return (uint64_t)-EINVAL;

    // ── Copy path ─────────────────────────────────────────────────────────
    char path[256];
    const char* upath = (const char*)path_ptr;
    uint64_t plen = 0;
    while (plen < 255 && upath[plen]) { path[plen] = upath[plen]; plen++; }
    path[plen] = '\0';
    if (plen == 0) return (uint64_t)-EINVAL;

    // ── Copy argv (up to 64 args) ─────────────────────────────────────────
    #define SPAWN_MAX_ARGS 64
    #define SPAWN_MAX_ARG_LEN 512
    char* k_argv[SPAWN_MAX_ARGS + 1];
    uint32_t argc = 0;
    if (argv_ptr) {
        const uint64_t* uargv = (const uint64_t*)argv_ptr;
        while (argc < SPAWN_MAX_ARGS) {
            uint64_t ustr = uargv[argc];
            if (!ustr) break;
            const char* s = (const char*)ustr;
            uint64_t l = 0; while (l < SPAWN_MAX_ARG_LEN && s[l]) l++;
            char* ks = kmalloc(l + 1);
            if (!ks) goto oom_argv;
            __builtin_memcpy(ks, s, l);
            ks[l] = '\0';
            k_argv[argc++] = ks;
        }
    }
    k_argv[argc] = NULL;
    // If caller passed no argv, synthesise {path, NULL}.
    if (argc == 0) { k_argv[0] = path; k_argv[1] = NULL; argc = 1; }

    // ── Copy envp (up to 64 vars) ─────────────────────────────────────────
    #define SPAWN_MAX_ENVS 64
    char* k_envp[SPAWN_MAX_ENVS + 1];
    uint32_t envc = 0;
    if (envp_ptr) {
        const uint64_t* uenvp = (const uint64_t*)envp_ptr;
        while (envc < SPAWN_MAX_ENVS) {
            uint64_t ustr = uenvp[envc];
            if (!ustr) break;
            const char* s = (const char*)ustr;
            uint64_t l = 0; while (l < SPAWN_MAX_ARG_LEN && s[l]) l++;
            char* ks = kmalloc(l + 1);
            if (!ks) goto oom_envp;
            __builtin_memcpy(ks, s, l);
            ks[l] = '\0';
            k_envp[envc++] = ks;
        }
    }
    k_envp[envc] = NULL;
    // Default env if caller passed nothing.
    const char* def_envp[] = { "PATH=/bin", "HOME=/root", "TERM=linux", "PWD=/", NULL };
    const char* const* final_envp = envc ? (const char* const*)k_envp : def_envp;

    // ── Resolve stdio spec ────────────────────────────────────────────────
    // stdio_ptr points to int[3]; 0 = open tty0 for all.
    int stdio[3] = { -1, -1, -1 }; // default: inherit
    if (stdio_ptr) {
        const int* us = (const int*)stdio_ptr;
        stdio[0] = us[0]; stdio[1] = us[1]; stdio[2] = us[2];
    }

    // ── Launch ────────────────────────────────────────────────────────────
    uint32_t pid = pid_alloc();
    if (!pid) goto oom_envp;

    task_t* child = elf_exec_from_ext2(path, pid,
                                        (const char* const*)k_argv,
                                        final_envp, stdio);
    if (!child) {
        pid_free(pid);
        for (uint32_t i = 0; i < envc; i++) kfree(k_envp[i]);
        if (argv_ptr) for (uint32_t i = 0; i < argc; i++) kfree(k_argv[i]);
        return (uint64_t)-ENOENT;
    }

    // Wire parent-child relationship so waitpid works correctly.
    child->ppid = g_current->pid;

    // ── Apply spawn_attr if provided ──────────────────────────────────────
    // spawn_attr_t is now ~32 bytes (unveil is a separate pointer), so it's
    // safe to copy directly onto the kernel stack.
    if (attr_ptr) {
        if (!_access_ok(attr_ptr, sizeof(spawn_attr_t))) goto bad_attr;
        spawn_attr_t a;
        __builtin_memcpy(&a, (const void*)attr_ptr, sizeof(spawn_attr_t));

        if (a.flags & SPAWN_ATTR_CRED) {
            child->cred.ruid = a.uid;
            child->cred.euid = a.uid;
            child->cred.suid = a.uid;
            child->cred.rgid = a.gid;
            child->cred.egid = a.gid;
            child->cred.sgid = a.gid;
        }
        if (a.flags & SPAWN_ATTR_UMASK) {
            child->umask = a.umask & 0777u;
        }
        if (a.flags & SPAWN_ATTR_PLEDGE) {
            child->pledge_mask = pledge_restrict(g_current->pledge_mask,
                                                 a.pledge_mask);
        }
        if ((a.flags & SPAWN_ATTR_UNVEIL) && a.nunveil && a.unveil) {
            uint32_t n = a.nunveil;
            uint64_t uptr = (uint64_t)(uintptr_t)a.unveil;
            if (!_access_ok(uptr, (uint64_t)n * sizeof(spawn_unveil_entry_t)))
                goto bad_attr;
            // Copy unveil entries one page-safe chunk at a time — each entry
            // is 257 bytes; kmalloc the whole array then walk it.
            spawn_unveil_entry_t* ue = kmalloc((uint64_t)n * sizeof(spawn_unveil_entry_t));
            if (!ue) goto bad_attr;
            __builtin_memcpy(ue, a.unveil, (uint64_t)n * sizeof(spawn_unveil_entry_t));
            unveil_free(&child->unveil);
            unveil_init(&child->unveil);
            for (uint32_t i = 0; i < n; i++)
                unveil_add(&child->unveil, ue[i].path, ue[i].perms);
            unveil_lock(&child->unveil);
            kfree(ue);
        }
    }
    bad_attr:;

    // Job control: if the child inherits the parent's tty (stdio[0]==-1),
    // give the terminal to the child's process group immediately.
    // This is what a proper shell does after fork+exec: tcsetpgrp(tty, child_pgid).
    // Without this, bash detects it's not in the foreground and spins on SIGTTOU.
    if (!stdio_ptr || (((const int*)stdio_ptr)[0] == -1)) {
        tty_t* tty = tty_get_ctty();
        if (!tty) tty = &g_tty0;
        tty->fg_pgid = child->pgid;
    }

    task_child_add(g_current, child);
    sched_add(child);

    for (uint32_t i = 0; i < envc; i++) kfree(k_envp[i]);
    if (argv_ptr) for (uint32_t i = 0; i < argc; i++) kfree(k_argv[i]);
    return (uint64_t)pid;

oom_envp:
    for (uint32_t i = 0; i < envc; i++) kfree(k_envp[i]);
oom_argv:
    if (argv_ptr) for (uint32_t i = 0; i < argc; i++) kfree(k_argv[i]);
    return (uint64_t)-ENOMEM;
}

// ── sys_thread ────────────────────────────────────────────────────────────
// thread(entry_ptr, stack_top, flags) → tid, or -1 on error.
// flags: THREAD_SHARE_MM (1<<0) — share address space
//        THREAD_SHARE_FILES (1<<1) — share fd table
// The caller must allocate the thread's user stack and pass its top.
static uint64_t sys_thread(uint64_t entry_ptr, uint64_t stack_top, uint64_t flags) {
    if (!g_current) return (uint64_t)-EINVAL;

    task_t* t = kmalloc(sizeof(task_t));
    if (!t) return (uint64_t)-ENOMEM;

    t->pid = pid_alloc();
    if (!t->pid) { kfree(t); return (uint64_t)-ENOMEM; }

    // Address space: share or deep-copy.
    if (flags & THREAD_SHARE_MM) {
        t->mm_shared = g_current->mm_shared;
        __atomic_add_fetch(&t->mm_shared->refs, 1, __ATOMIC_RELAXED);
    } else {
        phys_addr_t new_pml4 = vmm_alloc_pml4();
        if (new_pml4 == PMM_INVALID_ADDR) { pid_free(t->pid); kfree(t); return (uint64_t)-ENOMEM; }
        // CoW clone: pass mm so private pages are CoW-shared, not deep-copied.
        if (!vmm_clone_user_ex(new_pml4, g_current->mm_shared->pml4_phys,
                               g_current->mm_shared->mm)) {
            vmm_free_user(new_pml4); pmm_buddy_free(new_pml4, 0);
            pid_free(t->pid); kfree(t); return (uint64_t)-ENOMEM;
        }
        // Same CoW-vs-stale-TLB shootdown as task_fork — see
        // kernel/proc/process.c.  Without it a multithreaded spawner
        // corrupts its own heap.
        {
            extern void tlb_flush_mm(task_mm_t* mm);
            tlb_flush_mm(g_current->mm_shared);
        }
        mm_t* new_mm = g_current->mm_shared->mm ? mm_clone(g_current->mm_shared->mm) : NULL;
        task_mm_t* tmm = task_mm_alloc(new_pml4, new_mm);
        if (!tmm) {
            if (new_mm) mm_destroy(new_mm);
            vmm_free_user(new_pml4); pmm_buddy_free(new_pml4, 0);
            pid_free(t->pid); kfree(t); return (uint64_t)-ENOMEM;
        }
        t->mm_shared = tmm;
    }

    // File descriptors: share or copy.
    if (flags & THREAD_SHARE_FILES) {
        t->files_shared = g_current->files_shared;
        __atomic_add_fetch(&t->files_shared->refs, 1, __ATOMIC_RELAXED);
    } else {
        task_files_t* files = task_files_alloc();
        if (!files) { task_mm_release(t->mm_shared); pid_free(t->pid); kfree(t); return (uint64_t)-ENOMEM; }
        fdtable_t* src = g_current->files_shared->ft;
        fd_table_init(files, src->cap);
        fdtable_t* dst = files->ft;
        for (uint32_t i = 0; i < src->cap; i++)
            dst->fd_table[i] = vfs_dup(src->fd_table[i]);
        t->files_shared = files;
    }

    t->tgid             = g_current->tgid;
    t->ppid             = g_current->ppid;
    t->pgid             = g_current->pgid;
    t->sid              = g_current->sid;
    t->flags            = TASK_FLAG_THREAD;
    t->state            = TASK_READY;
    t->next             = NULL;
    t->children         = NULL;
    t->child_next       = NULL;
    t->pg_prev = t->pg_next = NULL;
    t->tg_prev = t->tg_next = NULL;
    t->sid_prev = t->sid_next = NULL;
    t->home_cpu = 0;
    t->mlfq_level       = 0;
    t->mlfq_ticks_left  = 0;
    t->sigstate.pending = 0;
    // spawn() creates a fresh program image — SIG_DFL everywhere, empty
    // mask.  SIG_DFL == 0, so a memset of handlers[] is the init.  Not
    // doing this left handlers[] as slab garbage, which later faulted
    // iretq with a non-canonical rip on first signal delivery.
    t->sigstate.blocked = 0;
    t->sigstate.sigframe_rsp = 0;
    __builtin_memset(t->sigstate.handlers, 0, sizeof(t->sigstate.handlers));
    t->exit_code        = 0;
    t->sleep_until_ns   = 0;
    // Threads inherit the spawner's TLS pointer only as a placeholder —
    // the libc trampoline calls SYS_SET_FS with the thread's own block
    // before running user code.  Non-shared (spawn) tasks start clean.
    t->fs_base          = (flags & THREAD_SHARE_MM) ? g_current->fs_base : 0;
    // kmalloc gives slab garbage — every pointer-bearing field must be
    // initialized here.  A garbage signalfd_head made signal_send's
    // notify walk fault in kernel mode the first time a signal hit a
    // thread task.
    t->signalfd_head     = (void*)0;
    t->cleartid_addr     = 0;
    t->drm_bytes_charged = 0;
    t->drm_priority      = g_current->drm_priority;
    t->umask            = g_current->umask;

    // Inherit comm, credentials, pledge, unveil, and FPU state from parent.
    t->cwd = kmalloc(KPATH_MAX);
    if (t->cwd) __builtin_memcpy(t->cwd, g_current->cwd, KPATH_MAX);
    __builtin_memcpy(t->comm, g_current->comm, sizeof(t->comm));
    t->cred         = g_current->cred;
    t->pledge_mask  = g_current->pledge_mask;
    unveil_copy(&t->unveil, &g_current->unveil);
    __asm__ volatile("fxsave %0" : "=m"(t->ctx.fxsave_buf));

    // Build initial kernel stack frame.
    // context_switch → user_trampoline → iretq to ring 3.
    // r12 = user RIP (entry_ptr), r13 = user RSP (stack_top).
    virt_addr_t kstack_top = kstack_alloc();
    t->kstack_top = kstack_top;

    uint64_t* stk = (uint64_t*)kstack_top;
    *(--stk) = 0;                         // alignment
    *(--stk) = (uint64_t)user_trampoline;
    *(--stk) = 0;                         // rbx
    *(--stk) = 0;                         // rbp
    *(--stk) = entry_ptr;                 // r12 = user RIP
    *(--stk) = stack_top;                 // r13 = user RSP
    *(--stk) = 0;                         // r14
    *(--stk) = 0;                         // r15

    t->ctx.rsp = (uint64_t)stk;

    sched_add(t);
    return (uint64_t)t->pid;
}

// ── sys_wait ──────────────────────────────────────────────────────────────
// waitpid(pid, *status, options):
//   pid > 0 : wait for that specific child (must be a direct child).
//   pid == -1: wait for any direct child.
//   options & WNOHANG: return 0 immediately if no child ready.
// Returns reaped child pid, 0 (WNOHANG + not ready), or -errno.
//
// Uses the per-task children list instead of a global queue walk.
static uint64_t sys_wait(uint64_t pid_arg, uint64_t status_ptr, uint64_t options) {
    if (!g_current) return (uint64_t)-EINVAL;

    int64_t  signed_pid = (int64_t)pid_arg;
    uint32_t target_pid = (signed_pid == -1) ? 0 : (uint32_t)signed_pid;

    for (;;) {
        // Atomic drain + walk + splice.  Lock-free: one xchg grabs the
        // whole children chain into a private list, we walk it without
        // any other CPU touching it, and if a zombie matches we unlink
        // it and splice the survivors back via one CAS.  Any
        // cross-CPU writer (e.g. another task reparenting orphans to
        // init) that races with our drain lands on the freshly-empty
        // head; the next iteration picks it up.
        //
        // Memory-ordering contract: the xchg is ACQ_REL.  It pairs
        // with the CAS-release in every task_child_add / _chain call
        // ever made on this list.  Any writer that published a child
        // via task_child_add before our xchg is fully visible to us
        // on the private chain — including the child's state field
        // (x86 TSO plus the lock-chain through sched_wake/sched_sleep
        // gives us sequential consistency on every wake cycle).
        uint8_t  found = 0;
        task_t*  zombie = task_children_reap(g_current, target_pid, &found);

        if (zombie) {
            // Also remove from the home CPU's zombie list (locked
            // there).  Until this returns the zombie is still
            // referenceable by /proc/[pid] / kill(pid) etc.; afterwards
            // only this wait owns it.
            sched_reap_zombie(zombie->pid);

            int32_t  code       = zombie->exit_code;
            uint32_t child_pid  = zombie->pid;
            uint32_t child_pgid = zombie->pgid;

            // Job control: return terminal to parent's process group.
            tty_t* tty = tty_get_ctty();
            if (!tty) tty = &g_tty0;
            if (tty && tty->fg_pgid == child_pgid)
                tty->fg_pgid = g_current->pgid;

            if (status_ptr) {
                int* sp = (int*)(uintptr_t)status_ptr;
                *sp = (int)((code & 0xFF) << 8);
            }

            process_destroy(zombie);
            return (uint64_t)child_pid;
        }

        // No reapable zombie in the drained chain.
        //
        // ECHILD cases:
        //   target_pid != 0 && !found — no child with that pid at all.
        //   target_pid == 0 && children list is entirely empty (reload
        //     the head after the splice-back; if it's NULL we had zero
        //     children and none were added in the meantime).
        if (target_pid != 0 && !found) return (uint64_t)-ECHILD;
        if (target_pid == 0 &&
            __atomic_load_n(&g_current->children, __ATOMIC_ACQUIRE) == NULL)
            return (uint64_t)-ECHILD;

        if (options & WNOHANG) return 0;

        // Block until a child exits and wakes us.  sys_exit /
        // signal.c's fatal path funnels through signal_send(parent,
        // SIGCHLD), which always calls sched_wake under the target's
        // rq_lock.  If the wake arrived between our reap pass above
        // and here, sched_sleep observes wake_pending under rq_lock
        // and returns immediately without parking — the outer loop
        // then re-drains and sees the zombie.  If the wake arrives
        // after we've parked, sched_wake moves us off sleep_head and
        // back to the run queue, and we resume normally.
        sched_sleep();
    }
}

