// ── sys_ioctl ─────────────────────────────────────────────────────────────
// ioctl(fd, request, arg) → varies
static uint64_t sys_ioctl(uint64_t fd, uint64_t request, uint64_t arg) {
    if (!g_current) return (uint64_t)-EBADF;

    // Try the fd's own ioctl first.  fdget returns NULL for empty slots,
    // which is benign for the tty-fallback path below — except the old
    // code explicitly rejected closed fds > 2 here, so we mirror that
    // behaviour when the slot is empty.
    vfs_file_t* f = fdget(fd);
    if (f) {
        if (f->ioctl) {
            uint64_t r = (uint64_t)f->ioctl(f, request, arg);
            fdput(f);
            return r;
        }
        fdput(f);
    } else if (fd > 2) {
        return (uint64_t)-EBADF;
    }

    // Fallback: resolve to controlling tty (tty0 for console processes).
    tty_t* tty = &g_tty0;

    switch (request) {
    case TIOCGWINSZ:
        return (copy_to_user((void*)arg, &tty->winsize, sizeof(tty->winsize)) == 0)
               ? 0 : (uint64_t)-EFAULT;
    case TIOCSWINSZ: {
        winsize_t ws;
        if (copy_from_user(&ws, (const void*)arg, sizeof(ws)) != 0)
            return (uint64_t)-EFAULT;
        tty->winsize = ws;
        // Notify foreground pgrp of window size change.
        if (tty->fg_pgid) signal_send_pgrp(tty->fg_pgid, SIGWINCH);
        return 0;
    }
    case TIOCGPGRP:
        return (copy_to_user((void*)arg, &tty->fg_pgid, sizeof(tty->fg_pgid)) == 0)
               ? 0 : (uint64_t)-EFAULT;
    case TIOCSPGRP: {
        uint32_t pg = 0;
        if (copy_from_user(&pg, (const void*)arg, sizeof(pg)) != 0)
            return (uint64_t)-EFAULT;
        tty->fg_pgid = pg;
        return 0;
    }
    case TCGETS:
        return (copy_to_user((void*)arg, &tty->termios, sizeof(tty->termios)) == 0)
               ? 0 : (uint64_t)-EFAULT;
    case TCSETS:
    case TCSETSW:
        return (copy_from_user(&tty->termios, (const void*)arg, sizeof(tty->termios)) == 0)
               ? 0 : (uint64_t)-EFAULT;
    case TCSETSF: {
        int r = copy_from_user(&tty->termios, (const void*)arg, sizeof(tty->termios));
        tty_flush_input(tty);
        return (r == 0) ? 0 : (uint64_t)-EFAULT;
    }
    case TCSBRK:
    case TCXONC:
    case TCFLSH:
        tty_flush_input(tty);
        return 0;
    case TIOCEXCL:
    case TIOCNXCL:
        return 0;  // Acknowledged, no-op.
    case TIOCSCTTY:
        tty_set_ctty(tty);
        return 0;
    case TIOCGSERIAL:
        if (arg) {
            uint8_t* p = (uint8_t*)arg;
            __builtin_memset(p, 0, 60);
        }
        return 0;
    default:
        return (uint64_t)-EINVAL;
    }
}

// ── sys_select ────────────────────────────────────────────────────────────
// select(nfds, readfds*, writefds*, exceptfds*, timeval*) → count or -errno
// Simplified: we consider a fd readable if it has data waiting, writable
// if it's not full, and we do not sleep — we just poll once.
// A NULL fd_set means "don't care about this set".
// timeval = {0,0} → non-blocking; NULL → block until at least one is ready.
static int fd_is_readable(vfs_file_t* f) {
    if (!f || !f->read) return 0;
    // Pipes: check if data is available without blocking.
    if (f->poll) return f->poll(f, POLLIN);
    return 1; // regular files and devices are always ready
}

static int fd_is_writable(vfs_file_t* f) {
    if (!f || !f->write) return 0;
    if (f->poll) return f->poll(f, POLLOUT);
    return 1;
}

static int fd_has_hup(vfs_file_t* f) {
    if (!f || !f->poll) return 0;
    return f->poll(f, POLLHUP);
}

static uint64_t sys_select(uint64_t nfds, uint64_t rset_ptr, uint64_t wset_ptr,
                            uint64_t eset_ptr, uint64_t tv_ptr) {
    if (!g_current) return (uint64_t)-EINVAL;
    if (nfds > FD_SETSIZE) return (uint64_t)-EINVAL;

    // Read the input fd_sets through copy_from_user (== _access_ok + prefault).
    // A raw deref of rset_ptr/wset_ptr/eset_ptr would be an arbitrary kernel
    // read (a kernel address makes the selected bits observable in the returned
    // output sets) and a kernel-fault DoS on a bad pointer -- the input twin of
    // sys_poll's already-hardened copy_from_user path.  NULL = empty set.
    fd_set_t rset, wset, eset;
    fd_set_t rout, wout, eout;
    if (rset_ptr) { if (copy_from_user(&rset, (const void*)rset_ptr, sizeof(rset)) != 0) return (uint64_t)-EFAULT; }
    else __builtin_memset(&rset, 0, sizeof(rset));
    if (wset_ptr) { if (copy_from_user(&wset, (const void*)wset_ptr, sizeof(wset)) != 0) return (uint64_t)-EFAULT; }
    else __builtin_memset(&wset, 0, sizeof(wset));
    if (eset_ptr) { if (copy_from_user(&eset, (const void*)eset_ptr, sizeof(eset)) != 0) return (uint64_t)-EFAULT; }
    else __builtin_memset(&eset, 0, sizeof(eset));
    __builtin_memset(&rout, 0, sizeof(rout));
    __builtin_memset(&wout, 0, sizeof(wout));
    __builtin_memset(&eout, 0, sizeof(eout));

    // Determine timeout in nanoseconds (NULL = infinite).
    int sel_infinite = 1;
    uint64_t timeout_ns = 0;
    if (tv_ptr) {
        k_timeval_t tv;
        if (copy_from_user(&tv, (const void*)tv_ptr, sizeof(tv)) != 0)
            return (uint64_t)-EFAULT;   // was: ignored -> used uninitialized tv
        timeout_ns = (uint64_t)tv.tv_sec * 1000000000ULL
                   + (uint64_t)tv.tv_usec * 1000ULL;
        sel_infinite = 0;
    }

    extern uint64_t tsc_read_ns(void);
    uint64_t deadline = sel_infinite ? UINT64_MAX : (tsc_read_ns() + timeout_ns);
    int count = 0;

    do {
        count = 0;
        task_files_t* files = g_current->files_shared;
        for (uint32_t fd = 0; fd < nfds; fd++) {
            vfs_file_t* f = (fd < files->ft->cap) ? files->ft->fd_table[fd] : NULL;
            if (FD_ISSET(fd, &rset) && fd_is_readable(f)) {
                rout.bits[fd/64] |= (1ULL << (fd%64)); count++;
            }
            if (FD_ISSET(fd, &wset) && fd_is_writable(f)) {
                wout.bits[fd/64] |= (1ULL << (fd%64)); count++;
            }
            if (FD_ISSET(fd, &eset)) {
                // No exceptional conditions in our model.
            }
        }
        if (count > 0) break;
        if (!sel_infinite && timeout_ns == 0) break;

        // 2*nfds worst case (primary + secondary waitq per fd).  Allocate
        // both the task_we_t[] and wait_queue_t*[] in one buffer.
        uint32_t sel_nslots = nfds * 2;
        size_t   alloc_bytes = sel_nslots * (sizeof(task_we_t) + sizeof(wait_queue_t*));
        void*    sel_buf = kmalloc(alloc_bytes);
        if (sel_buf) {
            __builtin_memset(sel_buf, 0, alloc_bytes);
            task_we_t*     wes = (task_we_t*)sel_buf;
            wait_queue_t** wqs = (wait_queue_t**)(wes + sel_nslots);
            wait_group_t   wg;
            wait_group_init(&wg, wes, wqs, sel_nslots);

            for (uint32_t fd = 0; fd < nfds; fd++) {
                if (!FD_ISSET(fd, &rset) && !FD_ISSET(fd, &wset) &&
                    !FD_ISSET(fd, &eset)) continue;
                vfs_file_t* f = (fd < files->ft->cap) ? files->ft->fd_table[fd] : NULL;
                if (!f) continue;
                wait_group_add(&wg, f->waitq, g_current);
                wait_group_add(&wg, f->secondary_waitq, g_current);
            }

            // Re-check after registering — close race between first check and add.
            int sel_recheck = 0;
            for (uint32_t fd = 0; fd < nfds && !sel_recheck; fd++) {
                vfs_file_t* f = (fd < files->ft->cap) ? files->ft->fd_table[fd] : NULL;
                if (!f) continue;
                if ((FD_ISSET(fd, &rset) && fd_is_readable(f)) ||
                    (FD_ISSET(fd, &wset) && fd_is_writable(f))) sel_recheck = 1;
            }
            if (!sel_recheck) {
                g_current->sleep_until_ns = sel_infinite ? 0 : deadline;
                sched_sleep();
                g_current->sleep_until_ns = 0;
            }
            wait_group_cleanup(&wg);
            kfree(sel_buf);
        }
    } while (sel_infinite || tsc_read_ns() < deadline);

    // Write back output sets.
    if (rset_ptr) copy_to_user((void*)rset_ptr, &rout, sizeof(rout));
    if (wset_ptr) copy_to_user((void*)wset_ptr, &wout, sizeof(wout));
    if (eset_ptr) copy_to_user((void*)eset_ptr, &eout, sizeof(eout));

    return (uint64_t)(int64_t)count;
}

// ── sys_poll ──────────────────────────────────────────────────────────────
// poll(pollfd_t*, nfds, timeout_ms) → count or -errno
static uint64_t sys_poll(uint64_t fds_ptr, uint64_t nfds, uint64_t timeout_ms) {
    if (!g_current || !fds_ptr) return (uint64_t)-EINVAL;
    if (nfds > 1024) return (uint64_t)-EINVAL;

    task_files_t* files = g_current->files_shared;

    int infinite = (timeout_ms == (uint64_t)-1);
    uint64_t timeout_ns = infinite ? 0 : (timeout_ms * 1000000ULL);
    extern uint64_t tsc_read_ns(void);
    uint64_t deadline = infinite ? UINT64_MAX : (tsc_read_ns() + timeout_ns);
    int count = 0;

    // Copy the user pollfd array into a kernel buffer instead of dereferencing
    // the user pointer directly.  copy_from_user validates the whole range
    // (_access_ok + prefault), so a bad/kernel/non-canonical fds pointer now
    // returns -EFAULT rather than faulting the kernel, or worse granting an
    // arbitrary kernel read/write through the fds pointer (up to nfds*8).  The
    // copy also keeps the per-iteration re-poll off the user pointer.
    pollfd_t* kfds = NULL;
    uint64_t  fds_bytes = nfds * sizeof(pollfd_t);
    if (nfds > 0) {
        kfds = (pollfd_t*)kmalloc(fds_bytes);
        if (!kfds) return (uint64_t)-ENOMEM;
        if (copy_from_user(kfds, (const void*)fds_ptr, fds_bytes) != 0) {
            kfree(kfds);
            return (uint64_t)-EFAULT;
        }
    }

    do {
        count = 0;
        for (uint64_t i = 0; i < nfds; i++) {
            kfds[i].revents = 0;
            int fd = kfds[i].fd;
            if (fd < 0) continue;
            vfs_file_t* f = ((uint32_t)fd < files->ft->cap) ? files->ft->fd_table[fd] : NULL;
            if (!f) { kfds[i].revents = POLLNVAL; count++; continue; }

            uint16_t rev = 0;
            if ((kfds[i].events & POLLIN)  && fd_is_readable(f))  rev |= POLLIN;
            if ((kfds[i].events & POLLOUT) && fd_is_writable(f)) rev |= POLLOUT;
            // POLLHUP and POLLERR are always reported, regardless of the
            // caller's events mask (POSIX requirement).
            if (fd_has_hup(f)) rev |= POLLHUP;
            if (rev) { kfds[i].revents = rev; count++; }
        }
        if (count > 0) break;
        if (timeout_ms == 0) break;

        uint32_t p_nslots = (uint32_t)nfds * 2;
        size_t   p_bytes  = p_nslots * (sizeof(task_we_t) + sizeof(wait_queue_t*));
        void*    p_buf    = kmalloc(p_bytes);
        if (p_buf) {
            __builtin_memset(p_buf, 0, p_bytes);
            task_we_t*     wes = (task_we_t*)p_buf;
            wait_queue_t** wqs = (wait_queue_t**)(wes + p_nslots);
            wait_group_t   wg;
            wait_group_init(&wg, wes, wqs, p_nslots);

            for (uint64_t i = 0; i < nfds; i++) {
                int fd = kfds[i].fd;
                if (fd < 0) continue;
                vfs_file_t* f = ((uint32_t)fd < files->ft->cap)
                                ? files->ft->fd_table[fd] : NULL;
                if (!f) continue;
                wait_group_add(&wg, f->waitq, g_current);
                wait_group_add(&wg, f->secondary_waitq, g_current);
            }

            // Re-check after registering — close race between first check and add.
            int recheck = 0;
            for (uint64_t i = 0; i < nfds; i++) {
                int fd = kfds[i].fd;
                if (fd < 0) continue;
                vfs_file_t* f = ((uint32_t)fd < files->ft->cap) ? files->ft->fd_table[fd] : NULL;
                if (!f) continue;
                if (((kfds[i].events & POLLIN)  && fd_is_readable(f)) ||
                    ((kfds[i].events & POLLOUT) && fd_is_writable(f)) ||
                    fd_has_hup(f)) { recheck = 1; break; }
            }
            if (!recheck) {
                g_current->sleep_until_ns = infinite ? 0 : deadline;
                sched_sleep();
                g_current->sleep_until_ns = 0;
            }
            wait_group_cleanup(&wg);
            kfree(p_buf);
        }
    } while (infinite || tsc_read_ns() < deadline);

    // Publish the resulting revents back to user space (best-effort: the array
    // was already validated on the way in via copy_from_user).
    if (nfds > 0) {
        copy_to_user((void*)fds_ptr, kfds, fds_bytes);
        kfree(kfds);
    }
    return (uint64_t)(int64_t)count;
}

// ── epoll ─────────────────────────────────────────────────────────────────
//
// Linux-accurate epoll using wait.h callback design (see kernel/include/wait.h):
//
//   epoll_ctl(ADD): kmallocs an epoll_watch_t, inserts it into an open-addressing
//     hash table keyed by fd.  Registers persistent epoll_we_t entries on the
//     fd's waitq (and secondary_waitq if present).  epoll_we_t.func =
//     epoll_wake_func: sets state->has_ready=1 and wakes state->wq.  Returns
//     WQ_KEEP — stays in the file's waitq until DEL/close.
//
//   epoll_wait(): registers ONE stack-allocated task_we_t on state->wq and
//     sleeps.  When any watched fd fires: fd.waitq → epoll_wake_func →
//     state->wq → task_we_t wakes the task (WQ_REMOVE).
//     Zero per-wakeup allocations after ADD.
//
//   epoll_ctl(DEL/MOD): O(1) hash lookup, unlinks entries, frees watch.
//
//   epoll_close(): drains all watch slots and frees state.
//
// Hash table: open addressing, power-of-2 capacity, load factor ≤ 75%.
// Initial cap = 16.  Grow-only (no shrink).  Tombstones for DEL.
// Memory per epoll fd: sizeof(epoll_state_t) + cap*8 bytes for the slot array
// + sizeof(epoll_watch_t) per watched fd.  No 40KB fixed allocation.

// Sentinel: slot contains EPOLL_DELETED after a DEL (tombstone).
#define EPOLL_DELETED ((epoll_watch_t*)1uL)
#define EPOLL_HT_INIT_CAP 16u

typedef struct {
    int32_t      fd;
    uint32_t     events;    // EPOLLIN | EPOLLOUT | ...
    epoll_data_t data;
    // Persistent epoll_we_t entries registered on the watched fd's queues.
    epoll_we_t   entry;     // on wq1
    epoll_we_t   entry2;    // on wq2 (if has_entry2)
    int          has_entry2;
    // Backup pointers to the waitqs we're registered on, so we can
    // unregister even when the target vfs_file_t has already been
    // freed (close(fd) before epoll_close()).  Otherwise the entries
    // stay linked on long-lived global queues like g_mouse_waitq and
    // the next wake walks into our RCU-freed w → #GP.
    wait_queue_t* wq1;
    wait_queue_t* wq2;
} epoll_watch_t;

typedef struct {
    epoll_watch_t** slots;  // open-addressing hash table (NULL=empty, DELETED=tomb)
    uint32_t        cap;    // number of slots (power of 2)
    uint32_t        count;  // live watches
    wait_queue_t    wq;     // sleeping tasks
    int             has_ready;
    // The epoll fd's own vfs_file waitq (f->_waitq).  Handed to every
    // epoll_we_t so epoll_wake_func can propagate readiness to an
    // OUTER epoll watching this epfd (nested epoll).  Never NULL after
    // sys_epoll_create wires it.
    wait_queue_t*   file_wq;
    // Writer lock: serialises epoll_ctl against epoll_wait scans and
    // against other concurrent epoll_ctl calls on the same epfd.  NOT
    // IRQ-off: the wake callback (epoll_wake_func, which can fire from
    // an IRQ handler via a watched fd's waitq) only touches has_ready +
    // wq and never takes this lock, so there is no IRQ↔lock ordering
    // problem.  Keeping IRQs enabled means we can safely copy_to_user,
    // call f->poll, or kmalloc while holding it (though we still avoid
    // copy_to_user under the lock to minimise hold time).
    spinlock_t      lock;
} epoll_state_t;

// Hash table helpers.  Key = fd (int32_t, always ≥ 0).
static uint32_t ep_hash(int32_t fd, uint32_t cap) {
    return (uint32_t)((uint32_t)fd * 2654435761u) & (cap - 1u);
}

// Find the slot index for `fd`.  Returns cap if not found.
static uint32_t ep_find(epoll_state_t* st, int32_t fd) {
    uint32_t i = ep_hash(fd, st->cap);
    for (uint32_t n = 0; n < st->cap; n++) {
        epoll_watch_t* s = st->slots[i];
        if (!s) return st->cap;                     // empty — not found
        if (s != EPOLL_DELETED && s->fd == fd) return i;
        i = (i + 1u) & (st->cap - 1u);
    }
    return st->cap;
}

// Insert w into the hash table (caller ensures cap > count and fd not present).
static void ep_ht_insert_raw(epoll_watch_t** slots, uint32_t cap, epoll_watch_t* w) {
    uint32_t i = ep_hash(w->fd, cap);
    for (;;) {
        epoll_watch_t* s = slots[i];
        if (!s || s == EPOLL_DELETED) { slots[i] = w; return; }
        i = (i + 1u) & (cap - 1u);
    }
}

// Grow the hash table to new_cap (power of 2, > cap).
// Returns 0 on success, -ENOMEM on failure (old table unchanged).
static int ep_grow(epoll_state_t* st, uint32_t new_cap) {
    epoll_watch_t** new_slots = (epoll_watch_t**)kmalloc(
        (uint64_t)new_cap * sizeof(epoll_watch_t*));
    if (!new_slots) return -ENOMEM;
    __builtin_memset(new_slots, 0, (uint64_t)new_cap * sizeof(epoll_watch_t*));
    // Re-insert all live entries.
    for (uint32_t i = 0; i < st->cap; i++) {
        epoll_watch_t* s = st->slots[i];
        if (s && s != EPOLL_DELETED) ep_ht_insert_raw(new_slots, new_cap, s);
    }
    kfree(st->slots);
    st->slots = new_slots;
    st->cap   = new_cap;
    return 0;
}

// Register/unregister persistent epoll_we_t entries on the watched fd's queues.
static void epoll_watch_register(epoll_state_t* state, epoll_watch_t* w,
                                  vfs_file_t* f) {
    epoll_we_init(&w->entry, &state->wq, &state->has_ready, state->file_wq);
    epoll_we_add(f->waitq, &w->entry);
    w->wq1 = f->waitq;
    if (f->secondary_waitq) {
        epoll_we_init(&w->entry2, &state->wq, &state->has_ready, state->file_wq);
        epoll_we_add(f->secondary_waitq, &w->entry2);
        w->wq2 = f->secondary_waitq;
        w->has_entry2 = 1;
    } else {
        w->wq2        = NULL;
        w->has_entry2 = 0;
    }
}

static void epoll_watch_unregister(epoll_watch_t* w, vfs_file_t* f) {
    // Use the saved waitq pointers, not f->waitq — f may already be
    // freed if the target fd was closed before the epoll fd.  Without
    // this, our entries stay linked on global waitqs (g_mouse_waitq,
    // pty master_waitq, …) and the next wake walks freed-w memory.
    (void)f;
    if (w->wq1)                  epoll_we_remove(w->wq1, &w->entry);
    if (w->has_entry2 && w->wq2) epoll_we_remove(w->wq2, &w->entry2);
    w->wq1 = NULL;
    w->wq2 = NULL;
    w->has_entry2 = 0;
}

// RCU free helper for epoll_watch_t.  An epoll_watch_t's entry/entry2
// may be in a wait_queue_t chain that a concurrent wait_queue_wake_all
// on another CPU is currently walking as an RCU reader.  Plain kfree
// right after unregister is a use-after-free in that window.
// call_rcu defers the real kfree until every concurrent drainer has
// completed its RCU reader section.  See kernel/wait.c commentary.
static void epoll_watch_free_rcu(void* data) {
    kfree(data);
}

// RCU free helper for epoll_state_t (closing epoll fd).  The state is
// referenced by every epoll_we_t as `ewe->p_has_ready = &state->has_ready`
// and `ewe->epoll_wq = &state->wq`.  A concurrent wait_queue_wake_all
// VFS ops for the epoll fd itself.
static int64_t epoll_read (vfs_file_t* self, void* buf, uint64_t len) {
    (void)self; (void)buf; (void)len; return (int64_t)-EINVAL;
}
static int64_t epoll_write(vfs_file_t* self, const void* buf, uint64_t len) {
    (void)self; (void)buf; (void)len; return (int64_t)-EINVAL;
}
static int64_t epoll_seek(vfs_file_t* self, int64_t off, int w) {
    (void)self; (void)off; (void)w; return (int64_t)-ESPIPE;
}

// Poll op for the epoll fd itself — what makes one epoll watchable by
// another (libinput's context fd is a nested epfd; wlroots registers
// it in wl_event_loop's epoll).  Ready ⇔ some watched fd is ready
// right now.
//
// Without this, the no-poll-op default in the epoll_wait scan treated
// a nested epfd as PERMANENTLY readable: wlroots' loop saw the
// libinput epfd "ready" forever, libinput_dispatch() drained nothing,
// and every wlroots compositor busy-spun before its first frame — the
// dwl/sway black screen (a NULL vtable entry, hence the old
// "null-vtable" label).
//
// Locking: trylock only.  The caller may already hold an OUTER
// epoll's state->lock (its scan invoking f->poll on this epfd), so
// blocking here would deadlock on a watch cycle (A⊂B, B⊂A).  On
// contention report "not ready": level-triggered re-scans triggered by
// the epoll_wake_func→file_wq cascade make a missed ready transient.
// s_ep_poll_depth bounds A⊂B⊂C… chain recursion at 4 like Linux's
// ep_loop_check; balanced inc/dec on one CPU keeps it exact (kernel
// code is non-preemptive; poll ops never run from IRQ context).
static int s_ep_poll_depth[MAX_CPUS];

static int epoll_poll(vfs_file_t* self, int events) {
    if (!(events & POLLIN)) return 0;   // never writable, no HUP state
    epoll_state_t* st = (epoll_state_t*)self->ctx;
    if (!st || !g_current) return 0;

    unsigned cpu = cpu_id();
    if (s_ep_poll_depth[cpu] >= 4) return 0;
    if (!spin_trylock(&st->lock)) return 0;
    s_ep_poll_depth[cpu]++;

    task_files_t* files = g_current->files_shared;
    int ready = 0;
    for (uint32_t i = 0; i < st->cap && !ready; i++) {
        epoll_watch_t* w = st->slots[i];
        if (!w || w == EPOLL_DELETED) continue;
        vfs_file_t* f = ((uint32_t)w->fd < files->ft->cap)
                        ? files->ft->fd_table[w->fd] : NULL;
        if (!f) { ready = 1; break; }   // dangling fd → EPOLLERR|EPOLLHUP pending
        if (f->poll) {
            if (((w->events & EPOLLIN)  && f->poll(f, POLLIN))  ||
                ((w->events & EPOLLOUT) && f->poll(f, POLLOUT)) ||
                f->poll(f, POLLHUP))
                ready = 1;
        } else {
            // No poll op = regular-file semantics: always ready.
            if (w->events & (EPOLLIN | EPOLLOUT)) ready = 1;
        }
    }

    s_ep_poll_depth[cpu]--;
    spin_unlock(&st->lock);
    return ready;
}
static void epoll_close(vfs_file_t* self) {
    if (self->ctx) {
        epoll_state_t* state = (epoll_state_t*)self->ctx;
        task_files_t* files = g_current ? g_current->files_shared : NULL;
        for (uint32_t i = 0; i < state->cap; i++) {
            epoll_watch_t* w = state->slots[i];
            if (!w || w == EPOLL_DELETED) continue;
            vfs_file_t* f = (files && (uint32_t)w->fd < files->ft->cap)
                            ? files->ft->fd_table[w->fd] : NULL;
            epoll_watch_unregister(w, f);
            call_rcu_expedited(epoll_watch_free_rcu, w);  // user-syscall latency
        }
        kfree(state->slots);
        kfree(state);
    }
    kfree(self);
}

// epoll_create1(flags) → fd or -errno
static uint64_t sys_epoll_create(uint64_t flags) {
    (void)flags;
    if (!g_current) return (uint64_t)-EINVAL;

    epoll_state_t* state = (epoll_state_t*)kmalloc(sizeof(epoll_state_t));
    if (!state) return (uint64_t)-ENOMEM;

    state->slots = (epoll_watch_t**)kmalloc(
        (uint64_t)EPOLL_HT_INIT_CAP * sizeof(epoll_watch_t*));
    if (!state->slots) { kfree(state); return (uint64_t)-ENOMEM; }
    __builtin_memset(state->slots, 0,
                      (uint64_t)EPOLL_HT_INIT_CAP * sizeof(epoll_watch_t*));
    state->cap       = EPOLL_HT_INIT_CAP;
    state->count     = 0;
    state->has_ready = 0;
    spin_lock_init(&state->lock);
    wait_queue_init(&state->wq);

    vfs_file_t* f = (vfs_file_t*)kmalloc(sizeof(vfs_file_t));
    if (!f) { kfree(state->slots); kfree(state); return (uint64_t)-ENOMEM; }
    __builtin_memset(f, 0, sizeof(*f));

    f->read           = epoll_read;
    f->write          = epoll_write;
    f->close          = epoll_close;
    f->seek           = epoll_seek;
    f->poll           = epoll_poll;   // nested epoll: real readiness, not always-on
    f->ioctl          = NULL;
    f->ctx            = state;
    f->waitq           = &f->_waitq; wait_queue_init(f->waitq);
    state->file_wq     = f->waitq;    // wake target for outer epolls (see epoll_we_t)
    f->secondary_waitq = NULL;
    f->flags          = 0;
    f->refcount       = 1;
    f->rights         = 0;
    f->path[0]        = '\0';

    int64_t efd = fd_install(f);
    if (efd < 0) { epoll_close(f); return (uint64_t)(int64_t)efd; }
    return (uint64_t)efd;
}

// epoll_ctl(epfd, op, fd, event*) → 0 or -errno
static uint64_t sys_epoll_ctl(uint64_t epfd, uint64_t op, uint64_t fd,
                               uint64_t event_ptr) {
    if (!g_current) return (uint64_t)-EINVAL;

    vfs_file_t* ef = fd_to_file(epfd);
    if (!ef || ef->close != epoll_close) return (uint64_t)-EBADF;
    epoll_state_t* state = (epoll_state_t*)ef->ctx;

    epoll_event_t ev;
    if (op != EPOLL_CTL_DEL) {
        if (!event_ptr) return (uint64_t)-EINVAL;
        if (copy_from_user(&ev, (const void*)event_ptr, sizeof(ev)) != 0)
            return (uint64_t)-EFAULT;
    }

    int32_t tfd = (int32_t)fd;
    task_files_t* files = g_current->files_shared;

    spin_lock(&state->lock);
    uint64_t ret;
    switch (op) {
    case EPOLL_CTL_ADD: {
        if (ep_find(state, tfd) < state->cap) { ret = (uint64_t)-EEXIST; break; }
        vfs_file_t* wf = ((uint32_t)tfd < files->ft->cap)
                         ? files->ft->fd_table[tfd] : NULL;
        if (!wf) { ret = (uint64_t)-EBADF; break; }
        // Grow if at 75% load.
        if (state->count * 4u >= state->cap * 3u) {
            if (ep_grow(state, state->cap * 2u) < 0) { ret = (uint64_t)-ENOMEM; break; }
        }
        epoll_watch_t* w = (epoll_watch_t*)kmalloc(sizeof(epoll_watch_t));
        if (!w) { ret = (uint64_t)-ENOMEM; break; }
        w->fd         = tfd;
        w->events     = ev.events;
        w->data       = ev.data;
        w->has_entry2 = 0;
        w->wq1        = NULL;
        w->wq2        = NULL;
        epoll_watch_register(state, w, wf);
        ep_ht_insert_raw(state->slots, state->cap, w);
        state->count++;
        ret = 0;
        break;
    }
    case EPOLL_CTL_DEL: {
        uint32_t idx = ep_find(state, tfd);
        if (idx >= state->cap) { ret = (uint64_t)-ENOENT; break; }
        epoll_watch_t* w = state->slots[idx];
        vfs_file_t* wf = ((uint32_t)tfd < files->ft->cap)
                         ? files->ft->fd_table[tfd] : NULL;
        epoll_watch_unregister(w, wf);
        state->slots[idx] = EPOLL_DELETED;
        state->count--;
        call_rcu_expedited(epoll_watch_free_rcu, w);  // user-syscall latency
        ret = 0;
        break;
    }
    case EPOLL_CTL_MOD: {
        uint32_t idx = ep_find(state, tfd);
        if (idx >= state->cap) { ret = (uint64_t)-ENOENT; break; }
        epoll_watch_t* w = state->slots[idx];
        vfs_file_t* wf = ((uint32_t)tfd < files->ft->cap)
                         ? files->ft->fd_table[tfd] : NULL;
        epoll_watch_unregister(w, wf);
        w->events = ev.events;
        w->data   = ev.data;
        if (wf) epoll_watch_register(state, w, wf);
        ret = 0;
        break;
    }
    default:
        ret = (uint64_t)-EINVAL;
        break;
    }
    spin_unlock(&state->lock);
    return ret;
}

// epoll_wait(epfd, events_ptr, maxevents, timeout_ms) → count or -errno
static uint64_t sys_epoll_wait(uint64_t epfd, uint64_t events_ptr,
                                uint64_t maxevents, uint64_t timeout_ms) {
    if (!g_current) return (uint64_t)-EINVAL;
    if (!events_ptr || maxevents == 0 || maxevents > 1024) return (uint64_t)-EINVAL;

    vfs_file_t* ef = fd_to_file(epfd);
    if (!ef || ef->close != epoll_close) return (uint64_t)-EBADF;
    epoll_state_t* state = (epoll_state_t*)ef->ctx;

    task_files_t* files = g_current->files_shared;
    epoll_event_t* uevents = (epoll_event_t*)events_ptr;

    int infinite = (timeout_ms == (uint64_t)-1);
    extern uint64_t tsc_read_ns(void);
    uint64_t timeout_ns = infinite ? 0 : (timeout_ms * 1000000ULL);
    uint64_t deadline   = infinite ? UINT64_MAX : (tsc_read_ns() + timeout_ns);
    int count = 0;

    // Collect ready events into a temporary kernel buffer under state->lock,
    // then release the lock before copy_to_user.  This keeps the lock hold
    // time bounded to a pure in-memory scan and avoids running copy_to_user
    // (which may fault) with a spinlock held.  maxevents is capped to 1024
    // above, so the allocation is at most 16 KiB.
    epoll_event_t* kevents = (epoll_event_t*)kmalloc(
        (uint64_t)maxevents * sizeof(epoll_event_t));
    if (!kevents) return (uint64_t)-ENOMEM;

    // One task_we_t on the epoll's own wq — stack allocated, zero per-wakeup alloc.
    task_we_t task_we;

    do {
        count = 0;
        spin_lock(&state->lock);
        for (uint32_t i = 0; i < state->cap && (uint64_t)count < maxevents; i++) {
            epoll_watch_t* w = state->slots[i];
            if (!w || w == EPOLL_DELETED) continue;
            int32_t wfd = w->fd;

            vfs_file_t* f = ((uint32_t)wfd < files->ft->cap)
                            ? files->ft->fd_table[wfd] : NULL;
            if (!f) {
                kevents[count].events = EPOLLERR | EPOLLHUP;
                kevents[count].data   = w->data;
                count++;
                continue;
            }

            uint32_t mask = w->events;
            uint32_t rev  = 0;
            if (f->poll) {
                if ((mask & EPOLLIN)  && f->poll(f, POLLIN))  rev |= EPOLLIN;
                if ((mask & EPOLLOUT) && f->poll(f, POLLOUT)) rev |= EPOLLOUT;
                if (f->poll(f, POLLHUP)) rev |= EPOLLHUP;
            } else {
                if (mask & EPOLLIN)  rev |= EPOLLIN;
                if (mask & EPOLLOUT) rev |= EPOLLOUT;
            }

            if (rev) {
                kevents[count].events = rev;
                kevents[count].data   = w->data;
                count++;
            }
        }
        spin_unlock(&state->lock);

        if (count > 0) break;
        if (timeout_ms == 0) break;

        // Canonical Phase 9-6 wait-queue registration, with the epoll-
        // specific has_ready fast-path flag on top.
        //
        // Ordering contract (see wait.c:epoll_wake_func for the paired
        // waker side).  The absolute critical rule is:
        //
        //   (1) clear has_ready RELAXED — it will be release-ordered by
        //       task_we_add's CAS below.
        //   (2) task_we_add (__ATOMIC_RELEASE on success) publishes the
        //       stack entry AND the has_ready clear in one op.
        //   (3) re-read has_ready with ACQUIRE, pairing with the
        //       waker's RELEASE store in epoll_wake_func.
        //   (4) fall back to a full poll re-scan under state->lock if
        //       the flag didn't catch it — the poll callback reads the
        //       actual fd state which is linearised by its own
        //       lock-free ring or per-queue lock.
        //
        // Every interleaving of a waker vs this sleeper maps to one
        // of:
        //   (a) waker's has_ready store is globally visible before the
        //       sleeper's ACQUIRE load → sleeper sees 1, skips sleep.
        //   (b) waker's task_we xchg on state->wq happens after our
        //       CAS on the same cache line → waker drains our entry
        //       → task_wake_func → sched_wake → wake_pending → sched_sleep
        //       bails.
        //   (c) waker's data push committed but has_ready and wake
        //       both raced against our registration → poll re-scan
        //       under state->lock reads the authoritative ring state
        //       and returns ready.
        // There is no interleaving that loses a wake.
        __atomic_store_n(&state->has_ready, 0, __ATOMIC_RELAXED);
        task_we_init(&task_we, g_current);
        task_we_add(&state->wq, &task_we);

        int ep_recheck = __atomic_load_n(&state->has_ready, __ATOMIC_ACQUIRE);
        if (!ep_recheck) {
            spin_lock(&state->lock);
            for (uint32_t i = 0; i < state->cap && !ep_recheck; i++) {
                epoll_watch_t* w = state->slots[i];
                if (!w || w == EPOLL_DELETED) continue;
                int32_t wfd = w->fd;
                vfs_file_t* f = ((uint32_t)wfd < files->ft->cap)
                                ? files->ft->fd_table[wfd] : NULL;
                if (!f || !f->poll) continue;
                uint32_t mask = w->events;
                if ((mask & EPOLLIN)  && f->poll(f, POLLIN))  ep_recheck = 1;
                if ((mask & EPOLLOUT) && f->poll(f, POLLOUT)) ep_recheck = 1;
                if (f->poll(f, POLLHUP))                      ep_recheck = 1;
            }
            spin_unlock(&state->lock);
        }
        if (!ep_recheck) {
            g_current->sleep_until_ns = infinite ? 0 : deadline;
            sched_sleep();
            g_current->sleep_until_ns = 0;
        }
        task_we_remove(&state->wq, &task_we);

    } while (infinite || tsc_read_ns() < deadline);

    // Copy the collected events out to user space, outside of any lock.
    if (count > 0) {
        if (copy_to_user(uevents, kevents,
                          (uint64_t)count * sizeof(epoll_event_t)) != 0) {
            kfree(kevents);
            return (uint64_t)-EFAULT;
        }
    }
    kfree(kevents);
    return (uint64_t)(int64_t)count;
}

