/* Per-comm syscall tracing — DEBUGGING.md §2 + §4.
 *
 * Every syscall enter/exit fires a TRACE event into the per-CPU ring
 * (cheap, always on, dumped on panic).  When the caller's comm matches
 * one of the SYSCALL_TRACE_COMMS below, we ALSO pr_debug a human-
 * readable line to serial — useful for diagnosing a specific program
 * (like foot blanking after wayland connect) without drowning bash /
 * dwl traffic.  Flip SYSCALL_TRACE_ALL to log every comm.
 */
// ── native_syscall_dispatch ───────────────────────────────────────────────
// Dispatches using our internal syscall numbers via a jump table.
uint64_t native_syscall_dispatch(uint64_t nr, uint64_t arg1, uint64_t arg2,
                                  uint64_t arg3, uint64_t arg4) {
    uint64_t ret;

    /* Snapshot arg5/arg6 from the per-CPU scratch into the TASK.  The
     * asm stashed them in this_cpu() via gs-relative stores, which is
     * only valid as long as we don't migrate CPUs.  mmap is a 6-arg
     * syscall whose intermediate paths (ext2 read, vmm_page_map) can
     * sleep, and if we reschedule onto a different CPU, a subsequent
     * read of this_cpu()->syscall_arg5 would see whatever OTHER task's
     * arg5 was most recently written to THAT cpu's slot — typically
     * stdin (fd=0), which is exactly the corruption we observed.
     *
     * Snapshotting here, while still on the entry CPU, pins the real
     * values to a per-task slot that follows the task across
     * migrations. */
    if (g_current) {
        // Read from the per-task kernel-stack frame, not this_cpu():
        // a preemption between the entry asm's sti and this line can
        // migrate the task, after which the per-CPU slots belong to
        // whatever task last entered a syscall on the new CPU.
        syscall_kframe_t* kf = SYSCALL_KFRAME(g_current);
        g_current->syscall_a5 = kf->r8;
        g_current->syscall_a6 = kf->r9;
    }

    /* Trace enter.  TRACE() is per-CPU + lock-free + always on — the
     * ring is the cheapest available instrument and the panic path
     * dumps it.  pr_debug is heavier (serial lock + formatting) so we
     * only emit it for comms we care about. */
    TRACE(TRACE_SYSCALL_ENTER, nr, arg1, arg2, arg3);
    int traced = 0;
    if (g_current && comm_match(g_current->comm)) {
        traced = 1;
        pr_debug("sys", "→ %s nr=%lu a1=0x%lx a2=0x%lx a3=0x%lx a4=0x%lx",
                  g_current->comm, nr, arg1, arg2, arg3, arg4);
    }
    // Pre-9-7 this mirrored every stdout/stderr byte to port 0x3F8 as a
    // debug aid.  That's catastrophically wrong under SMP: per-byte
    // unlocked `inb(0x3F8+5); outb(0x3F8, c)` from every CPU on every
    // write(2) hangs the UART state machine, spins forever on the TX-
    // empty bit, and (observed in GDB) parks whichever task happens to
    // be writing mid-busy-poll.  The cost is also enormous on the hot
    // path: bash writing "hello\n" to stdout was 6 port-polled byte
    // spins per write, serialised through a shared non-locked port.
    //
    // The mirror is gone.  Kernel-side debug goes through the locked
    // serial_*_dbg helpers in common.h (called from cold paths like
    // [exit]/[open] logging), which is bounded and safe.  User-space
    // stdout/stderr goes to the real tty / pty.  Serial debug output
    // from the kernel is still available; it's just not every single
    // byte of every user write.

    sys_handler_t h = (nr < (uint64_t)(sizeof(s_syscall_table) / sizeof(s_syscall_table[0])))
                      ? s_syscall_table[nr] : NULL;
    ret = h ? h(arg1, arg2, arg3, arg4) : (uint64_t)-ENOSYS;

    TRACE(TRACE_SYSCALL_EXIT, nr, ret, 0, 0);
    if (traced) {
        pr_debug("sys", "← %s nr=%lu → 0x%lx",
                  g_current ? g_current->comm : "?", nr, ret);
    }

    // Deliver any pending signals on the syscall return path.  This is
    // the one site that may set up a user signal frame — we own this
    // task's SYSCALL_KFRAME and are about to return through it.
    signal_deliver_pending(1, ret);
    return ret;
}

// ── syscall_dispatch ──────────────────────────────────────────────────────
// Entry point from syscall_entry.asm.
// Enforces pledge() before dispatching any handler.
uint64_t syscall_dispatch(uint64_t nr, uint64_t arg1, uint64_t arg2,
                          uint64_t arg3, uint64_t arg4) {
    // ── Pledge enforcement ────────────────────────────────────────────────
    // Check before ANY handler runs.  Violation → SIGKILL (immediate, no
    // handler, no audit trail beyond the kill event itself).
    if (g_current && g_current->pledge_mask != PLEDGE_ALL) {
        uint32_t need = pledge_group_for_syscall(nr);
        if (need != 0 && !pledge_allowed(g_current->pledge_mask, need)) {
            signal_send(g_current, SIGKILL);
            // signal_deliver_pending will run on return; return a value that
            // will never be observed (process is dying).
            return (uint64_t)-EPERM;
        }
        // SYS_OPEN: finer-grained pledge check based on access mode.
        if (nr == SYS_OPEN) {
            int oflags = (int)(arg2 & 3);   // O_RDONLY=0, O_WRONLY=1, O_RDWR=2
            int creat  = (arg2 & O_CREAT) != 0;
            uint32_t need_open = 0;
            if (oflags == O_RDONLY && !creat) need_open = PLEDGE_RPATH;
            else if (creat)                   need_open = PLEDGE_CPATH;
            else                              need_open = PLEDGE_WPATH;
            if (!pledge_allowed(g_current->pledge_mask, need_open)) {
                signal_send(g_current, SIGKILL);
                return (uint64_t)-EPERM;
            }
        }
        // SYS_MMAP: PROT_EXEC requires PLEDGE_PROT_EXEC.
        if (nr == SYS_MMAP) {
            uint64_t prot = arg3;
            if ((prot & 4 /* PROT_EXEC */) &&
                !pledge_allowed(g_current->pledge_mask, PLEDGE_PROT_EXEC)) {
                signal_send(g_current, SIGKILL);
                return (uint64_t)-EPERM;
            }
        }
        // SYS_SOCKET: AF_INET vs AF_UNIX distinction.
        if (nr == SYS_SOCKET) {
            int domain = (int)arg1;
            uint32_t need_sock = (domain == 1 /* AF_UNIX */) ? PLEDGE_UNIX : PLEDGE_INET;
            if (!pledge_allowed(g_current->pledge_mask, need_sock)) {
                signal_send(g_current, SIGKILL);
                return (uint64_t)-EPERM;
            }
        }
        // SYS_IOCTL: tty vs non-tty.
        if (nr == SYS_IOCTL) {
            // Requests ≥ 0x5400 are tty ioctls.
            uint64_t req = arg2;
            uint32_t need_ioctl = (req >= 0x5400 && req <= 0x54FF)
                                  ? PLEDGE_TTY : PLEDGE_IOCTL;
            if (!pledge_allowed(g_current->pledge_mask, need_ioctl)) {
                signal_send(g_current, SIGKILL);
                return (uint64_t)-EPERM;
            }
        }
    }

    return native_syscall_dispatch(nr, arg1, arg2, arg3, arg4);
}
